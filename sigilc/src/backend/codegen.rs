//! Backend: Program + Graph IR → ownership-safe Rust crate.
//! Level-1 guarantees are preserved by construction in the emitted code.

use crate::analysis::ir::GraphIR;
use crate::frontend::ast::{
    BinOp, Expr, Literal, OnHandler, Process, Program, Schema, Stmt, Tag, Type,
};
use std::collections::BTreeSet;

/// Emit a full Rust library source from the checked program and its Graph IR.
pub fn emit(program: &Program, irs: &[GraphIR]) -> String {
    let ir = &merged_header_ir(irs);
    let mut out = String::new();
    out.push_str("// AUTO-GENERATED by sigilc — do not edit by hand\n");
    out.push_str("//\n");
    out.push_str("// Hardening applied to this crate:\n");
    out.push_str("//   #![forbid(unsafe_code)]  — no escape hatch exists here\n");
    out.push_str("//   overflow-checks = true   — in EVERY profile (see Cargo.toml).\n");
    out.push_str("//     Silent wrapping would let an attacker roll a counter past\n");
    out.push_str("//     i64::MAX and silently break a proven invariant. An overflow is\n");
    out.push_str("//     fail-stop: Tokio reports the task panic through its JoinHandle.\n");
    out.push_str("#![forbid(unsafe_code)]\n");
    out.push_str("#![deny(clippy::mem_forget)]\n\n");
    out.push_str("//\n");
    out.push_str(&format!("// Process: {}\n", ir.process_name));
    out.push_str(&format!(
        "// Local state: {}\n",
        if ir.local_states.is_empty() {
            "(none)".into()
        } else {
            ir.local_states.join(", ")
        }
    ));
    let timeouts: Vec<_> = ir
        .nodes
        .iter()
        .filter_map(|n| match n {
            crate::analysis::ir::Node::Timeout { ms, .. } => Some(format!("{ms}ms")),
            _ => None,
        })
        .collect();
    if !timeouts.is_empty() {
        out.push_str(&format!("// Timed stages: {}\n", timeouts.join(", ")));
    }
    out.push_str("//\n");
    out.push_str("// Level-1 guarantees preserved by construction:\n");
    out.push_str("//   - process-local state only (no shared mutability)\n");
    out.push_str("//   - no null values\n");
    out.push_str("//   - every @timeout has an explicit @recover path\n\n");

    out.push_str("// Observability is OPT-IN: build with `--features tracing` to get a\n");
    out.push_str("// span per handler invocation. The default build pulls in no extra\n");
    out.push_str("// dependency, because a generated crate should not force one on a\n");
    out.push_str("// codebase that has already chosen its stack.\n");
    out.push_str("#[cfg(feature = \"tracing\")]\nuse tracing::instrument;\n\n");
    out.push_str("use std::time::Duration;\n");
    out.push_str("use tokio::time::timeout;\n");
    out.push_str("use sigil_rt::Result;\n\n");

    for schema in &program.schemas {
        out.push_str(&emit_schema(schema));
        out.push('\n');
    }

    let guards = build_input_guards(program);
    let routes = build_send_routes(program);
    for process in &program.processes {
        out.push_str(&emit_process(process, &guards, &routes));
        out.push('\n');
        out.push_str(&emit_actor(process));
        out.push('\n');
    }

    let primary_ty = primary_message_type(program);
    let (_env, transform_types) = crate::analysis::types::infer_program(program);

    // Prefer declared transforms: pure bodies compile; empty bodies are residual stubs.
    let mut emitted: BTreeSet<String> = BTreeSet::new();
    if !program.transforms.is_empty() {
        out.push_str("// --- declared transforms ---\n");
        for t in &program.transforms {
            out.push_str(&emit_declared_transform(t));
            out.push('\n');
            emitted.insert(t.name.clone());
        }
    }

    let externals = collect_externals(program);
    let leftover: Vec<_> = externals.difference(&emitted).cloned().collect();
    if !leftover.is_empty() {
        out.push_str("// --- undeclared transforms (residual stubs) ---\n");
        for name in leftover {
            let (in_ty, out_ty) = transform_types
                .get(&name)
                .cloned()
                .unwrap_or_else(|| (primary_ty.clone(), primary_ty.clone()));
            out.push_str(&emit_external_stub(&name, &in_ty, &out_ty));
            out.push('\n');
        }
    }

    out.push_str(&emit_smoke_test(program));
    out
}

/// Merge per-process IRs into a summary view for the generated-file header
/// (names, states, timed stages). Analysis never uses this merged view.
fn merged_header_ir(irs: &[GraphIR]) -> GraphIR {
    let mut m = GraphIR {
        process_name: irs
            .iter()
            .map(|i| i.process_name.clone())
            .collect::<Vec<_>>()
            .join(", "),
        process_span: None,
        local_states: vec![],
        nodes: vec![],
        edges: vec![],
        external_calls: vec![],
    };
    for ir in irs {
        m.local_states.extend(ir.local_states.iter().cloned());
        m.nodes.extend(ir.nodes.iter().cloned());
        m.external_calls.extend(ir.external_calls.iter().cloned());
    }
    m
}

fn emit_declared_transform(t: &crate::frontend::ast::TransformDecl) -> String {
    use crate::frontend::ast::BindKind;
    let in_ty = rust_type(&t.param_ty);
    let out_ty = rust_type(&t.return_ty);
    let param = &t.param;

    // A bound transform calls real code, so the generated crate is complete:
    // nothing here needs hand-editing, and regenerating never clobbers work.
    if let Some(b) = &t.binding {
        let path = &b.path;
        let body = match b.kind {
            BindKind::Async => format!(
                "    {path}(input)\n        .await\n        .map_err(|e| sigil_rt::SigilError::Transform(format!(\"{}: {{e}}\", \"{}\")))",
                "{}", t.name
            ),
            // The important one. Calling a blocking function directly from an
            // async handler stalls a runtime worker — a failure that degrades
            // the scheduler rather than the program, so no proof here would
            // catch it. Declaring `blocking` moves it to the blocking pool.
            BindKind::Blocking => format!(
                "    tokio::task::spawn_blocking(move || {path}(input))\n                 \x20       .await\n                 \x20       .map_err(|e| sigil_rt::SigilError::Transform(format!(\"{}: join: {{e}}\", \"{}\")))?\n                 \x20       .map_err(|e| sigil_rt::SigilError::Transform(format!(\"{}: {{e}}\", \"{}\")))",
                "{}", t.name, "{}", t.name
            ),
            BindKind::Infallible => format!("    Ok({path}(input))"),
        };
        // An `infallible` binding is declared as unable to fail, so it is not
        // routed through fault injection — injecting faults into a recovery
        // path would defeat the reason it exists.
        let chaos = if matches!(b.kind, BindKind::Infallible) {
            String::new()
        } else {
            format!(
                "    sigil_rt::chaos::external_stage(\"{}\").await?;\n",
                t.name
            )
        };
        return format!(
            "// bound to `{path}` ({})\nasync fn {}(input: {in_ty}) -> Result<{out_ty}> {{\n{chaos}{body}\n}}\n",
            match b.kind {
                BindKind::Async => "async",
                BindKind::Blocking => "blocking, dispatched to the blocking pool",
                BindKind::Infallible => "infallible",
            },
            t.name
        );
    }

    if t.body.is_empty() {
        return emit_external_stub(&t.name, &in_ty, &out_ty);
    }
    let mut body_src = String::new();
    for stmt in &t.body {
        match stmt {
            Stmt::Let { name, expr, .. } => {
                body_src.push_str(&format!(
                    "    let {name} = {};\n",
                    emit_expr(expr, &[], param)
                ));
            }
            Stmt::Expr { expr, .. } => {
                body_src.push_str(&format!(
                    "    return Ok({});\n",
                    emit_expr(expr, &[], param)
                ));
            }
            Stmt::Assign { .. } => {
                body_src.push_str("    /* unsupported assign in transform */\n");
            }
            Stmt::Send { .. } => {
                body_src.push_str("    /* send is only legal in process handlers */\n");
            }
        }
    }
    if !body_src.contains("return Ok") {
        body_src.push_str(&format!("    Ok({param})\n"));
    }
    format!(
        "async fn {}({param}: {in_ty}) -> Result<{out_ty}> {{\n{body_src}}}\n",
        t.name
    )
}

fn primary_message_type(program: &Program) -> String {
    program
        .processes
        .first()
        .and_then(|p| p.handlers.first())
        .map(|h| rust_type(&h.msg_ty))
        .unwrap_or_else(|| "()".into())
}

fn emit_schema(schema: &Schema) -> String {
    // A bound schema IS the foreign type. Defining a structurally identical
    // struct here would produce a DIFFERENT type, and every call into the
    // bound crate would fail to typecheck.
    if let Some(path) = &schema.binding {
        return format!(
            "// `{}` is `{path}` from a bound crate; it must derive Clone, Debug\n             // and Default for the generated actors and demo driver.\npub use {path} as {};\n",
            schema.name, schema.name
        );
    }
    let mut s = String::new();
    s.push_str("#[derive(Clone, Debug, Default)]\n");
    s.push_str(&format!("pub struct {} {{\n", schema.name));
    for (fname, fty) in &schema.fields {
        s.push_str(&format!("    pub {}: {},\n", fname, rust_type(fty)));
    }
    s.push_str("}\n");
    s
}

/// Processes this process sends to, in declaration order (deduped).
fn process_send_targets(process: &Process) -> Vec<String> {
    let mut targets: Vec<String> = Vec::new();
    for handler in &process.handlers {
        for stmt in &handler.body {
            if let Stmt::Send { target, .. } = stmt {
                if !targets.contains(target) {
                    targets.push(target.clone());
                }
            }
        }
    }
    targets
}

/// Precompute runtime guard lines per (process, handler msg): every Level-3
/// input assumption `require msg.field <cmp> lit` is enforced at handler
/// entry, so proofs never rest on an unchecked assumption. Violations are
/// typed Schema errors, counted as drops by the actor loop.
fn build_input_guards(
    program: &Program,
) -> std::collections::BTreeMap<(String, String), Vec<String>> {
    use crate::analysis::level3::input_preconditions;
    let mut map: std::collections::BTreeMap<(String, String), Vec<String>> =
        std::collections::BTreeMap::new();

    // Finiteness guards on EVERY Float field of every inbound message.
    // Float is not currently in the Level-3/4 proof fragment, but rejecting
    // NaN and infinities at the actor boundary gives executable schemas a
    // predictable finite-input contract.
    for process in &program.processes {
        for handler in &process.handlers {
            let Type::Named(schema_name) = &handler.msg_ty else {
                continue;
            };
            let Some(schema) = program.schemas.iter().find(|sc| sc.name == *schema_name) else {
                continue;
            };
            for (fname, fty) in &schema.fields {
                if !matches!(fty, Type::Float) {
                    continue;
                }
                map.entry((process.name.clone(), handler.msg_name.clone()))
                    .or_default()
                    .push(format!(
                        "        // Reject non-finite input: NaN and +/-inf are outside the\n        // numeric model the Level-3 proofs are stated over.\n        if !{}.{fname}.is_finite() {{\n            return Err(sigil_rt::SigilError::Schema);\n        }}\n",
                        handler.msg_name
                    ));
            }
        }
    }
    for pc in input_preconditions(program) {
        // Field type decides the literal / cast shape.
        let field_ty = program
            .processes
            .iter()
            .find(|p| p.name == pc.process)
            .and_then(|p| p.handlers.iter().find(|h| h.msg_name == pc.msg_name))
            .and_then(|h| match &h.msg_ty {
                Type::Named(n) => program.schemas.iter().find(|sc| sc.name == *n),
                _ => None,
            })
            .and_then(|sc| sc.fields.iter().find(|(f, _)| *f == pc.field))
            .map(|(_, ty)| ty.clone());
        let (lhs, bound) = match (field_ty, pc.pred.bound) {
            (Some(Type::Int), crate::analysis::level3::NumericBound::Int(value)) => (
                format!("{}.{}", pc.msg_name, pc.field),
                format!("{value}i64"),
            ),
            (Some(Type::Float), crate::analysis::level3::NumericBound::Float(value)) => (
                format!("{}.{}", pc.msg_name, pc.field),
                format!("{value:?}f64"),
            ),
            _ => continue,
        };
        let op = match pc.pred.op {
            BinOp::Ge => ">=",
            BinOp::Gt => ">",
            BinOp::Le => "<=",
            BinOp::Lt => "<",
            _ => continue,
        };
        let line = format!(
            "        // Runtime-enforced spec precondition (spec {}): reject out-of-contract input\n        if !({lhs} {op} {bound}) {{\n            return Err(sigil_rt::SigilError::Schema);\n        }}\n",
            pc.spec
        );
        map.entry((pc.process.clone(), pc.msg_name.clone()))
            .or_default()
            .push(line);
    }
    map
}

fn emit_process(
    process: &Process,
    guards: &std::collections::BTreeMap<(String, String), Vec<String>>,
    routes: &SendRoutes,
) -> String {
    let moves = analyse_moves(process);
    let states: Vec<String> = process.states.iter().map(|s| s.name.clone()).collect();
    let mut s = String::new();

    let targets = process_send_targets(process);
    s.push_str(&format!("pub struct {} {{\n", process.name));
    for st in &process.states {
        // Public so integration tests / callers can observe local state.
        s.push_str(&format!("    pub {}: {},\n", st.name, rust_type(&st.ty)));
    }
    if !targets.is_empty() {
        s.push_str("    /// Outbound messages shed by back-pressure policy.\n");
        s.push_str("    pub __shed: u64,\n");
    }
    for t in &targets {
        s.push_str(&format!(
            "    /// Outbox to the {t} actor. Wire with connect_{}() before spawn.\n",
            t.to_lowercase()
        ));
        s.push_str(&format!(
            "    pub {}_out: Option<sigil_rt::Router<{t}Handle>>,\n",
            t.to_lowercase()
        ));
    }
    s.push_str("}\n\n");

    s.push_str(&format!("impl {} {{\n", process.name));
    s.push_str("    pub fn new() -> Self {\n        Self {\n");
    for st in &process.states {
        s.push_str(&format!(
            "            {}: {},\n",
            st.name,
            emit_expr(&st.init, &[], "")
        ));
    }
    if !targets.is_empty() {
        s.push_str("            __shed: 0,\n");
    }
    for t in &targets {
        s.push_str(&format!("            {}_out: None,\n", t.to_lowercase()));
    }
    s.push_str("        }\n    }\n\n");
    for t in &targets {
        s.push_str(&format!(
            "    pub fn connect_{}(&mut self, shards: Vec<{t}Handle>) -> Result<()> {{\n        self.{}_out = Some(sigil_rt::Router::new(shards)?);\n        Ok(())\n    }}\n\n",
            t.to_lowercase(),
            t.to_lowercase()
        ));
    }

    for handler in &process.handlers {
        let g = guards
            .get(&(process.name.clone(), handler.msg_name.clone()))
            .cloned()
            .unwrap_or_default();
        s.push_str(&emit_handler(handler, &states, &g, routes, &moves));
        s.push('\n');
    }
    s.push_str("}\n");
    s
}

/// Emit shared-nothing actor machinery for a process.
///
/// `__shed_of` is emitted per process so the spawn loop can report shed
/// counts uniformly whether or not the process sends anything.
///
/// This is where the Level-1 "no shared mutable state" guarantee is realized
/// in the generated Rust, not just checked by the compiler:
///   - `spawn(self)` MOVES the state into an isolated tokio task; after that
///     no reference to it can exist anywhere else in the program.
///   - The only way in is a message via a Clone-able `Handle` (an mpsc Sender).
///   - The only way out is `join()` after the channel closes.
///
/// There is no `Arc`, no `Mutex`, and no way to observe state concurrently.
fn emit_actor(process: &Process) -> String {
    if process.handlers.is_empty() {
        return String::new();
    }
    let p = &process.name;
    let mut s = String::new();

    // Shed counts live on the process itself when it has outboxes; emit the
    // expression inline so no cross-process helper names can collide.
    let shed_expr = if process_send_targets(process).is_empty() {
        "0"
    } else {
        "self.__shed"
    };
    let drop_outboxes: String = process_send_targets(process)
        .iter()
        .map(|t| {
            format!(
                "            self.{}_out = None; // release downstream channel\n",
                t.to_lowercase()
            )
        })
        .collect();
    if process.handlers.len() == 1 {
        let h = &process.handlers[0];
        let msg_ty = rust_type(&h.msg_ty);
        let method = format!("on_{}", h.msg_name);
        s.push_str(&format!(
            "/// Clone-able, thread-safe handle to a running `{p}` actor.\n\
             /// Sending is the ONLY way to interact with the actor's state.\n\
             #[derive(Clone)]\n\
             pub struct {p}Handle {{\n\
             \x20   tx: tokio::sync::mpsc::Sender<{msg_ty}>,\n\
             }}\n\n\
             impl {p}Handle {{\n\
             \x20   /// Raw channel, for the declared back-pressure policy.\n\
             \x20   pub fn raw(&self) -> &tokio::sync::mpsc::Sender<{msg_ty}> {{\n\
             \x20       &self.tx\n\
             \x20   }}\n\n\
             \x20   pub async fn send(&self, msg: {msg_ty}) -> Result<()> {{\n\
             \x20       self.tx\n\
             \x20           .send(msg)\n\
             \x20           .await\n\
             \x20           .map_err(|_| sigil_rt::SigilError::ActorStopped)\n\
             \x20   }}\n\
             }}\n\n\
             impl {p} {{\n\
             \x20   /// Move this process's state into an isolated task.\n\
             \x20   /// After `spawn`, the state is unreachable except via messages;\n\
             \x20   /// it is returned intact when the last Handle is dropped.\n\
             \x20   pub fn spawn(mut self, capacity: usize) -> Result<({p}Handle, tokio::task::JoinHandle<(Self, sigil_rt::ActorStats)>)> {{\n\
             \x20       sigil_rt::validate_channel_capacity(capacity)?;\n\
             \x20       let (tx, mut rx) = tokio::sync::mpsc::channel::<{msg_ty}>(capacity);\n\
             \x20       let join = tokio::spawn(async move {{\n\
             \x20           let mut stats = sigil_rt::ActorStats::default();\n\
             \x20           while let Some(msg) = rx.recv().await {{\n\
             \x20               match self.{method}(msg).await {{\n\
             \x20                   Ok(()) => stats.handled += 1,\n\
             \x20                   Err(_) => stats.dropped += 1, // residual stage failed with no recover path\n\
             \x20               }}\n\
             \x20           }}\n\
{drop_outboxes}\
             \x20           stats.shed = {shed_expr};\n\
             \x20           (self, stats)\n\
             \x20       }});\n\
             \x20       Ok(({p}Handle {{ tx }}, join))\n\
             \x20   }}\n\
             }}\n"
        ));
        return s;
    }

    // Multiple handlers: dispatch enum, one variant per message type.
    let enum_name = format!("{p}Msg");
    s.push_str(&format!("pub enum {enum_name} {{\n"));
    for h in &process.handlers {
        s.push_str(&format!(
            "    {}({}),\n",
            variant_name(&h.msg_name),
            rust_type(&h.msg_ty)
        ));
    }
    s.push_str("}\n\n");

    s.push_str(&format!(
        "#[derive(Clone)]\npub struct {p}Handle {{\n    tx: tokio::sync::mpsc::Sender<{enum_name}>,\n}}\n\nimpl {p}Handle {{\n    /// Raw channel, for the declared back-pressure policy.\n    pub fn raw(&self) -> &tokio::sync::mpsc::Sender<{enum_name}> {{\n        &self.tx\n    }}\n\n"
    ));
    for h in &process.handlers {
        s.push_str(&format!(
            "    pub async fn send_{}(&self, msg: {}) -> Result<()> {{\n        self.tx.send({enum_name}::{}(msg)).await.map_err(|_| sigil_rt::SigilError::ActorStopped)\n    }}\n",
            h.msg_name,
            rust_type(&h.msg_ty),
            variant_name(&h.msg_name)
        ));
    }
    s.push_str("}\n\n");

    s.push_str(&format!(
        "impl {p} {{\n    pub fn spawn(mut self, capacity: usize) -> Result<({p}Handle, tokio::task::JoinHandle<(Self, sigil_rt::ActorStats)>)> {{\n        sigil_rt::validate_channel_capacity(capacity)?;\n        let (tx, mut rx) = tokio::sync::mpsc::channel::<{enum_name}>(capacity);\n        let join = tokio::spawn(async move {{\n            let mut stats = sigil_rt::ActorStats::default();\n            while let Some(msg) = rx.recv().await {{\n                let r = match msg {{\n"
    ));
    for h in &process.handlers {
        s.push_str(&format!(
            "                    {enum_name}::{}(m) => self.on_{}(m).await,\n",
            variant_name(&h.msg_name),
            h.msg_name
        ));
    }
    s.push_str(&format!(
        "                }};\n                match r {{\n                    Ok(()) => stats.handled += 1,\n                    Err(_) => stats.dropped += 1,\n                }}\n            }}\n{drop_outboxes}            stats.shed = {shed_expr};\n            (self, stats)\n        }});\n        Ok(({p}Handle {{ tx }}, join))\n    }}\n}}\n"
    ));
    s
}

/// snake_case handler name → UpperCamelCase enum variant (`new_order` →
/// `NewOrder`), so generated code is idiomatic and lint-clean.
pub(crate) fn variant_name(msg_name: &str) -> String {
    msg_name
        .split('_')
        .filter(|seg| !seg.is_empty())
        .map(|seg| {
            let mut c = seg.chars();
            match c.next() {
                Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
                None => String::new(),
            }
        })
        .collect()
}

fn emit_handler(
    handler: &OnHandler,
    states: &[String],
    guards: &[String],
    routes: &SendRoutes,
    moves: &MoveAnalysis,
) -> String {
    let msg_ty = rust_type(&handler.msg_ty);
    let method = format!("on_{}", handler.msg_name);
    let mut s = String::new();
    // The span carries the process and message type, which is what an
    // on-call engineer needs to correlate a trace with the topology graph.
    s.push_str(&format!(
        "    #[cfg_attr(feature = \"tracing\", instrument(skip_all, fields(msg = \"{}\")))]\n",
        handler.msg_name
    ));
    s.push_str(&format!(
        "    pub async fn {method}(&mut self, {}: {msg_ty}) -> Result<()> {{\n",
        handler.msg_name
    ));
    for g in guards {
        s.push_str(g);
    }

    for stmt in &handler.body {
        s.push_str(&emit_stmt(
            stmt,
            "        ",
            states,
            &handler.msg_name,
            routes,
            moves,
        ));
    }

    s.push_str("        Ok(())\n");
    s.push_str("    }\n");
    s
}

/// Names still needed after a given statement.
///
/// Copying a message on every hand-off is the obvious way to stay sound and
/// the expensive way to run. This computes, per handler, whether each
/// binding is read again after the current statement; the last reader can
/// MOVE the value instead of cloning it. Being wrong here cannot corrupt
/// anything — it produces a borrow-check error in the generated crate, which
/// the build catches immediately.
#[derive(Default, Clone)]
pub struct MoveAnalysis {
    /// span start of a statement → names safe to move out of it
    movable: std::collections::BTreeMap<usize, std::collections::BTreeSet<String>>,
}

impl MoveAnalysis {
    fn can_move(&self, stmt_span: usize, name: &str) -> bool {
        self.movable
            .get(&stmt_span)
            .map(|s| s.contains(name))
            .unwrap_or(false)
    }
}

fn stmt_span_start(stmt: &Stmt) -> usize {
    match stmt {
        Stmt::Let { span, .. }
        | Stmt::Assign { span, .. }
        | Stmt::Send { span, .. }
        | Stmt::Expr { span, .. } => span.start,
    }
}

fn stmt_expr(stmt: &Stmt) -> &Expr {
    match stmt {
        Stmt::Let { expr, .. }
        | Stmt::Assign { expr, .. }
        | Stmt::Send { expr, .. }
        | Stmt::Expr { expr, .. } => expr,
    }
}

/// Every identifier read by an expression, with multiplicity.
fn count_reads(e: &Expr, out: &mut std::collections::BTreeMap<String, usize>) {
    match e {
        Expr::Ident { name, .. } => {
            *out.entry(name.clone()).or_insert(0) += 1;
        }
        Expr::FieldAccess { base, .. } => {
            *out.entry(base.clone()).or_insert(0) += 1;
        }
        Expr::Binary { lhs, rhs, .. } => {
            count_reads(lhs, out);
            count_reads(rhs, out);
        }
        Expr::If {
            cond,
            then_branch,
            else_branch,
            ..
        } => {
            count_reads(cond, out);
            count_reads(then_branch, out);
            count_reads(else_branch, out);
        }
        Expr::SchemaLit { fields, .. } => {
            for (_, fe) in fields {
                count_reads(fe, out);
            }
        }
        Expr::Call { args, .. } => {
            for a in args {
                count_reads(a, out);
            }
        }
        Expr::Pipeline { base, steps, .. } => {
            count_reads(base, out);
            for st in steps {
                count_reads(&st.expr, out);
            }
        }
        Expr::Literal { .. } => {}
    }
}

fn analyse_moves(process: &Process) -> MoveAnalysis {
    use std::collections::{BTreeMap, BTreeSet};
    let mut ma = MoveAnalysis::default();
    for handler in &process.handlers {
        let n = handler.body.len();
        for (i, stmt) in handler.body.iter().enumerate() {
            // Reads performed by later statements.
            let mut later: BTreeMap<String, usize> = BTreeMap::new();
            for laterstmt in handler.body.iter().take(n).skip(i + 1) {
                count_reads(stmt_expr(laterstmt), &mut later);
                if let Stmt::Send { guard, route, .. } = laterstmt {
                    if let Some(g) = guard {
                        count_reads(g, &mut later);
                    }
                    if let crate::frontend::ast::Route::ByKey(k) = route {
                        count_reads(k, &mut later);
                    }
                }
            }
            // Reads performed by THIS statement (a name read twice here must
            // still be cloned on all but its final occurrence; conservatively
            // require exactly one read to allow a move).
            let mut here: BTreeMap<String, usize> = BTreeMap::new();
            count_reads(stmt_expr(stmt), &mut here);
            if let Stmt::Send { guard, route, .. } = stmt {
                if let Some(g) = guard {
                    count_reads(g, &mut here);
                }
                if let crate::frontend::ast::Route::ByKey(k) = route {
                    count_reads(k, &mut here);
                }
            }
            let movable: BTreeSet<String> = here
                .iter()
                .filter(|(name, count)| **count == 1 && !later.contains_key(*name))
                .map(|(name, _)| name.clone())
                .collect();
            ma.movable.insert(stmt_span_start(stmt), movable);
        }
    }
    ma
}

/// Resolution of every `send` statement in the program: statement span →
/// the Handle method to call. Built from the verified topology so codegen
/// and the checker can never disagree about which handler receives a
/// message.
#[derive(Default)]
pub struct SendRoutes {
    /// span start → template that wraps the value for the destination's
    /// channel: `{v}` for single-handler targets, `TargetMsg::Variant({v})`
    /// for multi-handler ones.
    by_span: std::collections::BTreeMap<usize, String>,
}

impl SendRoutes {
    fn wrap(&self, target: &str, span_start: usize, value: &str) -> String {
        match self.by_span.get(&span_start) {
            Some(tpl) => tpl.replace("{v}", value),
            None => {
                debug_assert!(false, "unresolved send to {target} at byte {span_start}");
                value.to_string()
            }
        }
    }
}

fn build_send_routes(program: &Program) -> SendRoutes {
    let mut routes = SendRoutes::default();
    let Ok(topo) = crate::analysis::topology::derive_topology(program) else {
        return routes;
    };
    for process in &program.processes {
        for handler in &process.handlers {
            for stmt in &handler.body {
                let Stmt::Send { target, span, .. } = stmt else {
                    continue;
                };
                let Some(dest) = program.processes.iter().find(|p| p.name == *target) else {
                    continue;
                };
                let multi = dest.handlers.len() > 1;
                // Unique edge for this (from, to) pair unless the source sends
                // several types to the same multi-handler target; in that case
                // re-resolve by the sent value's local type.
                let candidates: Vec<&crate::analysis::topology::TopologyEdge> = topo
                    .edges
                    .iter()
                    .filter(|e| e.from == process.name && e.to == *target)
                    .collect();
                let to_handler = if candidates.len() == 1 {
                    candidates[0].to_handler.clone()
                } else {
                    match resolve_send_handler(program, handler, stmt, dest) {
                        Some(h) => h,
                        None => continue,
                    }
                };
                let tpl = if multi {
                    format!("{target}Msg::{}({{v}})", variant_name(&to_handler))
                } else {
                    "{v}".to_string()
                };
                routes.by_span.insert(span.start, tpl);
            }
        }
    }
    routes
}

/// Re-resolve a send's destination handler by the sent value's local type
/// (only needed when one process sends several message types to the same
/// multi-handler target).
fn resolve_send_handler(
    program: &Program,
    handler: &OnHandler,
    stmt: &Stmt,
    dest: &Process,
) -> Option<String> {
    let Stmt::Send { expr, .. } = stmt else {
        return None;
    };
    let sigs: std::collections::BTreeMap<&str, String> = program
        .transforms
        .iter()
        .map(|t| (t.name.as_str(), type_name_of(&t.return_ty)))
        .collect();
    let mut env: std::collections::BTreeMap<String, String> = std::collections::BTreeMap::new();
    env.insert(handler.msg_name.clone(), type_name_of(&handler.msg_ty));
    for st in &handler.body {
        if let Stmt::Let { name, expr, .. } = st {
            if let Some(t) = expr_type(expr, &env, &sigs) {
                env.insert(name.clone(), t);
            }
        }
    }
    let ty = expr_type(expr, &env, &sigs)?;
    dest.handlers
        .iter()
        .find(|h| type_name_of(&h.msg_ty) == ty)
        .map(|h| h.msg_name.clone())
}

fn expr_type(
    e: &Expr,
    env: &std::collections::BTreeMap<String, String>,
    sigs: &std::collections::BTreeMap<&str, String>,
) -> Option<String> {
    match e {
        Expr::Ident { name, .. } => env.get(name).cloned(),
        Expr::Call { name, .. } => sigs.get(name.as_str()).cloned(),
        Expr::Pipeline { base, steps, .. } => {
            let mut cur = expr_type(base, env, sigs);
            for step in steps {
                let t = match &step.expr {
                    Expr::Ident { name, .. } | Expr::Call { name, .. } => Some(name.as_str()),
                    _ => None,
                };
                cur = t.and_then(|n| sigs.get(n)).cloned();
            }
            cur
        }
        _ => None,
    }
}

fn type_name_of(t: &Type) -> String {
    crate::analysis::types::type_name(t)
}

fn emit_stmt(
    stmt: &Stmt,
    indent: &str,
    states: &[String],
    msg: &str,
    routes: &SendRoutes,
    moves: &MoveAnalysis,
) -> String {
    match stmt {
        Stmt::Let { name, expr, .. } => {
            // Prefer multi-line for timeout pipelines so the recover path is readable.
            if is_timed_pipeline(expr) {
                format!(
                    "{indent}let {name} = {};\n",
                    emit_expr_multiline(expr, states, msg, indent)
                )
            } else {
                format!("{indent}let {name} = {};\n", emit_expr(expr, states, msg))
            }
        }
        Stmt::Assign { name, expr, .. } => {
            format!("{indent}self.{name} = {};\n", emit_expr(expr, states, msg))
        }
        Stmt::Send {
            target,
            expr,
            route,
            backpressure,
            guard,
            span,
        } => {
            let target_lc = target.to_lowercase();
            // Fan-out sends the SAME binding to several targets. Clone only
            // when the value is still needed afterwards; the last reader moves.
            let value = {
                let v = emit_expr(expr, states, msg);
                let needs_clone = match expr {
                    Expr::Ident { name, .. } => !moves.can_move(span.start, name),
                    Expr::FieldAccess { base, .. } => !moves.can_move(span.start, base),
                    _ => false,
                };
                if needs_clone {
                    format!("{v}.clone()")
                } else {
                    v
                }
            };
            let wrapped = routes.wrap(target, span.start, &value);

            // How the value reaches a shard's raw channel, per routing policy.
            let (borrow, pick): (&str, String) = match route {
                crate::frontend::ast::Route::RoundRobin => {
                    ("as_mut", "out.round_robin().raw()".to_string())
                }
                crate::frontend::ast::Route::ByKey(key) => {
                    let k = emit_expr(key, states, msg);
                    ("as_ref", format!("out.by_key(&{k}).raw()"))
                }
                crate::frontend::ast::Route::Broadcast => ("as_ref", String::new()),
            };
            let not_connected = format!(
                "None => return Err(sigil_rt::SigilError::Transform(\"outbox to {target} not connected\".into())),"
            );

            // Broadcast fans out to every shard; the policy applies per shard.
            // The value is bound once and cloned per shard, so it is not moved
            // on the first iteration.
            if matches!(route, crate::frontend::ast::Route::Broadcast) {
                let wrapped = routes.wrap(target, span.start, "__b.clone()");
                let per_shard = match backpressure {
                    crate::frontend::ast::Backpressure::Block => format!(
                        "sigil_rt::backpressure::block(h.raw(), {wrapped}).await?;"
                    ),
                    crate::frontend::ast::Backpressure::Shed => format!(
                        "if sigil_rt::backpressure::shed(h.raw(), {wrapped})? == sigil_rt::SendOutcome::Shed {{ __shed_n += 1; }}"
                    ),
                    crate::frontend::ast::Backpressure::Deadline(ms) => format!(
                        "if sigil_rt::backpressure::deadline(h.raw(), {wrapped}, {ms}).await? == sigil_rt::SendOutcome::Shed {{ __shed_n += 1; }}"
                    ),
                };
                return format!(
                    "{indent}let mut __shed_n: u64 = 0;\n\
                     {indent}match self.{target_lc}_out.as_ref() {{\n\
                     {indent}    Some(out) => {{\n\
                     {indent}        let __b = {value};\n\
                     {indent}        for h in out.shards() {{\n\
                     {indent}            {per_shard}\n\
                     {indent}        }}\n\
                     {indent}    }}\n\
                     {indent}    {not_connected}\n\
                     {indent}}}\n\
                     {indent}if __shed_n > 0 {{\n\
                     {indent}    self.__shed += __shed_n;\n\
                     {indent}    for _ in 0..__shed_n {{ sigil_rt::chaos::note_shed(\"{target}\"); }}\n\
                     {indent}}}\n"
                );
            }

            let emitted = match backpressure {
                crate::frontend::ast::Backpressure::Block => format!(
                    "{indent}match self.{target_lc}_out.{borrow}() {{\n\
                     {indent}    Some(out) => {{\n\
                     {indent}        sigil_rt::backpressure::block({pick}, {wrapped}).await?;\n\
                     {indent}    }}\n\
                     {indent}    {not_connected}\n\
                     {indent}}}\n"
                ),
                // Shed / deadline can drop by design: compute the outcome in a
                // closed scope so the outbox borrow ends before we count it.
                crate::frontend::ast::Backpressure::Shed => format!(
                    "{indent}let __outcome = match self.{target_lc}_out.{borrow}() {{\n\
                     {indent}    Some(out) => sigil_rt::backpressure::shed({pick}, {wrapped})?,\n\
                     {indent}    {not_connected}\n\
                     {indent}}};\n\
                     {indent}if __outcome == sigil_rt::SendOutcome::Shed {{\n\
                     {indent}    self.__shed += 1;\n\
                     {indent}    sigil_rt::chaos::note_shed(\"{target}\");\n\
                     {indent}}}\n"
                ),
                crate::frontend::ast::Backpressure::Deadline(ms) => format!(
                    "{indent}let __outcome = match self.{target_lc}_out.{borrow}() {{\n\
                     {indent}    Some(out) => sigil_rt::backpressure::deadline({pick}, {wrapped}, {ms}).await?,\n\
                     {indent}    {not_connected}\n\
                     {indent}}};\n\
                     {indent}if __outcome == sigil_rt::SendOutcome::Shed {{\n\
                     {indent}    self.__shed += 1;\n\
                     {indent}    sigil_rt::chaos::note_shed(\"{target}\");\n\
                     {indent}}}\n"
                ),
            };
            match guard {
                None => emitted,
                Some(cond) => {
                    // Conditional forwarding. Level 4 charges this send
                    // against the counter's increase under the same guard.
                    let c = emit_expr(cond, states, msg);
                    let inner = emitted
                        .lines()
                        .map(|l| format!("    {l}"))
                        .collect::<Vec<_>>()
                        .join("\n");
                    format!("{indent}if {c} {{\n{inner}\n{indent}}}\n")
                }
            }
        }
        Stmt::Expr { expr, .. } => {
            format!("{indent}let _ = {};\n", emit_expr(expr, states, msg))
        }
    }
}

fn is_timed_pipeline(expr: &Expr) -> bool {
    match expr {
        Expr::Pipeline { steps, .. } => steps
            .iter()
            .any(|s| s.tags.iter().any(|t| matches!(t, Tag::Timeout { .. }))),
        _ => false,
    }
}

fn emit_expr(expr: &Expr, states: &[String], msg: &str) -> String {
    match expr {
        Expr::Ident { name, .. } => {
            if states.iter().any(|s| s == name) {
                format!("self.{name}")
            } else {
                name.clone()
            }
        }
        Expr::FieldAccess { base, field, .. } => {
            if states.iter().any(|s| s == base) {
                format!("self.{base}.{field}")
            } else {
                format!("{base}.{field}")
            }
        }
        Expr::Literal { value, .. } => emit_literal(value),
        Expr::Call { name, args, .. } => {
            let a = args
                .iter()
                .map(|e| emit_expr(e, states, msg))
                .collect::<Vec<_>>()
                .join(", ");
            format!("{name}({a}).await?")
        }
        Expr::Binary { op, lhs, rhs, .. } => {
            let op_s = match op {
                BinOp::Add => "+",
                BinOp::Sub => "-",
                BinOp::Mul => "*",
                BinOp::Div => "/",
                BinOp::Le => "<=",
                BinOp::Ge => ">=",
                BinOp::Lt => "<",
                BinOp::Gt => ">",
                BinOp::Eq => "==",
            };
            format!(
                "({} {} {})",
                emit_expr(lhs, states, msg),
                op_s,
                emit_expr(rhs, states, msg)
            )
        }
        Expr::If {
            cond,
            then_branch,
            else_branch,
            ..
        } => format!(
            "if {} {{ {} }} else {{ {} }}",
            emit_expr(cond, states, msg),
            emit_expr(then_branch, states, msg),
            emit_expr(else_branch, states, msg)
        ),
        Expr::SchemaLit { name, fields, .. } => {
            let inits = fields
                .iter()
                .map(|(f, e)| format!("{f}: {}", emit_expr(e, states, msg)))
                .collect::<Vec<_>>()
                .join(", ");
            format!("{name} {{ {inits} }}")
        }
        Expr::Pipeline { base, steps, .. } => emit_pipeline(base, steps, states, msg, false, ""),
    }
}

fn emit_expr_multiline(expr: &Expr, states: &[String], msg: &str, indent: &str) -> String {
    match expr {
        Expr::Pipeline { base, steps, .. } => emit_pipeline(base, steps, states, msg, true, indent),
        other => emit_expr(other, states, msg),
    }
}

fn emit_pipeline(
    base: &Expr,
    steps: &[crate::frontend::ast::PipeStep],
    states: &[String],
    msg: &str,
    multiline: bool,
    indent: &str,
) -> String {
    // Pipelines borrow their input conceptually; in Rust terms the base is
    // cloned when it is a named binding so the handler can keep using it
    // (e.g. guarded state updates after the pipeline).
    let mut current = emit_expr(base, states, msg);
    if matches!(base, Expr::Ident { .. } | Expr::FieldAccess { .. }) {
        current = format!("{current}.clone()");
    }
    for step in steps {
        let transform = match &step.expr {
            Expr::Ident { name, .. } => name.clone(),
            Expr::Call { name, .. } => name.clone(),
            other => emit_expr(other, states, msg),
        };

        let timeout_ms = step.tags.iter().find_map(|t| match t {
            Tag::Timeout {
                expr:
                    Expr::Literal {
                        value: Literal::DurationMs(ms),
                        ..
                    },
                ..
            } => Some(*ms),
            _ => None,
        });
        let recover = step.tags.iter().find_map(|t| match t {
            Tag::Recover { with, .. } => match with {
                Expr::Ident { name, .. } => Some(name.clone()),
                Expr::Call { name, .. } => Some(name.clone()),
                _ => None,
            },
            _ => None,
        });
        let has_error = step.tags.iter().any(|t| matches!(t, Tag::Error { .. }));
        let retries: u64 = step
            .tags
            .iter()
            .find_map(|t| match t {
                Tag::Retry {
                    expr:
                        Expr::Literal {
                            value: Literal::Int(n),
                            ..
                        },
                    ..
                } => Some((*n).max(0) as u64),
                _ => None,
            })
            .unwrap_or(0);

        if let (Some(ms), true) = (timeout_ms, has_error) {
            // Acknowledged timed drop: no fallback; timeout/failure propagates
            // (counted as dropped by the actor loop), after bounded retries.
            current = format!(
                "{{ let __in = {current}; let mut __attempt: u64 = 0; loop {{ match timeout(Duration::from_millis({ms}), {transform}(__in.clone())).await {{ Ok(Ok(v)) => break v, _ if __attempt < {retries} => {{ __attempt += 1; sigil_rt::chaos::note_retry(\"{transform}\"); }} Ok(Err(e)) => return Err(e), Err(_) => return Err(sigil_rt::SigilError::Timeout), }} }} }}"
            );
        } else if let (Some(ms), true) = (timeout_ms, retries > 0) {
            let fallback = recover.clone().unwrap_or_else(|| transform.clone());
            // Bounded re-attempts, then the recover path. Worst case
            // (1 + retries) x timeout is what Level-2 charges the budget.
            current = if multiline {
                format!(
                    "{{\n\
{indent}    let __in = {current};\n\
{indent}    let mut __attempt: u64 = 0;\n\
{indent}    loop {{\n\
{indent}        match timeout(Duration::from_millis({ms}), {transform}(__in.clone())).await {{\n\
{indent}            Ok(Ok(v)) => break v,\n\
{indent}            _ if __attempt < {retries} => {{\n\
{indent}                __attempt += 1;\n\
{indent}                sigil_rt::chaos::note_retry(\"{transform}\");\n\
{indent}            }}\n\
{indent}            _ => {{\n\
{indent}                sigil_rt::chaos::note_recovery(\"{transform}\");\n\
{indent}                break {fallback}(__in).await?;\n\
{indent}            }}\n\
{indent}        }}\n\
{indent}    }}\n\
{indent}}}"
                )
            } else {
                format!(
                    "{{ let __in = {current}; let mut __attempt: u64 = 0; loop {{ match timeout(Duration::from_millis({ms}), {transform}(__in.clone())).await {{ Ok(Ok(v)) => break v, _ if __attempt < {retries} => {{ __attempt += 1; sigil_rt::chaos::note_retry(\"{transform}\"); }} _ => {{ sigil_rt::chaos::note_recovery(\"{transform}\"); break {fallback}(__in).await?; }} }} }} }}"
                )
            };
        } else if let Some(ms) = timeout_ms {
            let fallback = recover.unwrap_or_else(|| transform.clone());
            if multiline {
                let inner = format!(
                    "{{\n\
{indent}    let __in = {current};\n\
{indent}    match timeout(Duration::from_millis({ms}), {transform}(__in.clone())).await {{\n\
{indent}        Ok(Ok(v)) => v,\n\
{indent}        Ok(Err(_)) | Err(_) => {{\n\
{indent}            sigil_rt::chaos::note_recovery(\"{transform}\");\n\
{indent}            {fallback}(__in).await?\n\
{indent}        }}\n\
{indent}    }}\n\
{indent}}}"
                );
                current = inner;
            } else {
                current = format!(
                    "({{ let __in = {current}; match timeout(Duration::from_millis({ms}), {transform}(__in.clone())).await {{ Ok(Ok(v)) => v, Ok(Err(_)) | Err(_) => {{ sigil_rt::chaos::note_recovery(\"{transform}\"); {fallback}(__in).await? }} }} }})"
                );
            }
        } else if let Some(fallback) = recover {
            if retries > 0 {
                // Untimed external stage: bounded re-attempts, then recover.
                current = format!(
                    "{{ let __in = {current}; let mut __attempt: u64 = 0; loop {{ match {transform}(__in.clone()).await {{ Ok(v) => break v, Err(_) if __attempt < {retries} => {{ __attempt += 1; sigil_rt::chaos::note_retry(\"{transform}\"); }} Err(_) => {{ sigil_rt::chaos::note_recovery(\"{transform}\"); break {fallback}(__in).await?; }} }} }} }}"
                );
            } else {
                // Untimed external stage with a declared recovery path.
                current = format!(
                    "({{ let __in = {current}; match {transform}(__in.clone()).await {{ Ok(v) => v, Err(_) => {{ sigil_rt::chaos::note_recovery(\"{transform}\"); {fallback}(__in).await? }} }} }})"
                );
            }
        } else {
            current = format!("{transform}({current}).await?");
        }
    }
    current
}

fn emit_literal(lit: &Literal) -> String {
    match lit {
        Literal::Int(i) => i.to_string(),
        Literal::Float(f) => {
            let s = format!("{f}");
            if s.contains('.') {
                s
            } else {
                format!("{s}.0")
            }
        }
        Literal::String(s) => format!("\"{s}\".to_string()"),
        Literal::Bool(b) => b.to_string(),
        Literal::DurationMs(ms) => format!("Duration::from_millis({ms})"),
    }
}

fn rust_type(ty: &Type) -> String {
    match ty {
        Type::Int => "i64".into(),
        Type::Float => "f64".into(),
        Type::String => "String".into(),
        Type::Bool => "bool".into(),
        Type::UUID => "String".into(),
        Type::Bytes => "Vec<u8>".into(),
        Type::Duration => "Duration".into(),
        Type::Named(n) => n.clone(),
    }
}

fn emit_external_stub(name: &str, in_ty: &str, out_ty: &str) -> String {
    // External residual stages route through sigil_rt::chaos so injected
    // latency and faults exercise the verified @timeout/@recover paths.
    if in_ty == out_ty {
        format!(
            "async fn {name}(input: {in_ty}) -> Result<{out_ty}> {{\n    sigil_rt::chaos::external_stage(\"{name}\").await?;\n    Ok(input)\n}}\n"
        )
    } else {
        // Stage changes schema: build a default output (residual: real logic belongs here).
        format!(
            "async fn {name}(_input: {in_ty}) -> Result<{out_ty}> {{\n    sigil_rt::chaos::external_stage(\"{name}\").await?;\n    Ok({out_ty}::default())\n}}\n"
        )
    }
}

fn collect_externals(program: &Program) -> BTreeSet<String> {
    let mut set = BTreeSet::new();
    for process in &program.processes {
        let mut locals: BTreeSet<String> = process.states.iter().map(|s| s.name.clone()).collect();
        for handler in &process.handlers {
            locals.insert(handler.msg_name.clone());
            for stmt in &handler.body {
                match stmt {
                    Stmt::Let { name, expr, .. } => {
                        walk_externals(expr, &mut set);
                        locals.insert(name.clone());
                    }
                    Stmt::Assign { expr, .. }
                    | Stmt::Send { expr, .. }
                    | Stmt::Expr { expr, .. } => {
                        walk_externals(expr, &mut set);
                    }
                }
            }
        }
    }
    set
}

fn walk_externals(expr: &Expr, set: &mut BTreeSet<String>) {
    match expr {
        Expr::If {
            cond,
            then_branch,
            else_branch,
            ..
        } => {
            walk_externals(cond, set);
            walk_externals(then_branch, set);
            walk_externals(else_branch, set);
        }
        Expr::SchemaLit { fields, .. } => {
            for (_, e) in fields {
                walk_externals(e, set);
            }
        }
        Expr::Call { name, args, .. } => {
            set.insert(name.clone());
            for a in args {
                walk_externals(a, set);
            }
        }
        Expr::Pipeline { base, steps, .. } => {
            walk_externals(base, set);
            for step in steps {
                match &step.expr {
                    Expr::Ident { name, .. } | Expr::Call { name, .. } => {
                        set.insert(name.clone());
                    }
                    other => walk_externals(other, set),
                }
                for tag in &step.tags {
                    if let Tag::Recover {
                        with: Expr::Ident { name, .. },
                        ..
                    } = tag
                    {
                        set.insert(name.clone());
                    }
                }
            }
        }
        Expr::Binary { lhs, rhs, .. } => {
            walk_externals(lhs, set);
            walk_externals(rhs, set);
        }
        Expr::Ident { .. } | Expr::FieldAccess { .. } | Expr::Literal { .. } => {}
    }
}

fn emit_smoke_test(program: &Program) -> String {
    if program.processes.is_empty() {
        return String::new();
    }
    let process = &program.processes[0];
    let mut s = String::new();
    s.push_str("#[cfg(test)]\nmod tests {\n    use super::*;\n\n");
    s.push_str("    #[tokio::test]\n");
    s.push_str("    async fn smoke() {\n");
    s.push_str(&format!("        let mut p = {}::new();\n", process.name));
    if let Some(handler) = process.handlers.first() {
        let ty = rust_type(&handler.msg_ty);
        s.push_str(&format!("        let msg = {ty}::default();\n"));
        s.push_str(&format!(
            "        p.on_{}(msg).await.expect(\"handler should succeed\");\n",
            handler.msg_name
        ));
        // Observe local state was reachable (does not assert business values).
        for st in &process.states {
            s.push_str(&format!("        let _ = &p.{};\n", st.name));
        }
    }
    s.push_str("    }\n}\n");
    s
}

/// Field synthesis for a demo message: Int=1, Float=1.0, Bool=true, String
/// gets a unique id. Keeps aggregates checkable by construction.
fn demo_field_init(program: &Program, msg_ty: &Type, var: &str, indent: &str) -> String {
    use std::fmt::Write as _;
    let mut out = String::new();
    if let Type::Named(name) = msg_ty {
        if let Some(schema) = program.schemas.iter().find(|sc| sc.name == *name) {
            for (fname, fty) in &schema.fields {
                match fty {
                    Type::Int => {
                        let _ = writeln!(out, "{indent}{var}.{fname} = 1;");
                    }
                    Type::Float => {
                        let _ = writeln!(out, "{indent}{var}.{fname} = 1.0;");
                    }
                    Type::String => {
                        let _ = writeln!(
                            out,
                            "{indent}{var}.{fname} = format!(\"m-{{prod}}-{{i}}\");"
                        );
                    }
                    Type::Bool => {
                        let _ = writeln!(out, "{indent}{var}.{fname} = true;");
                    }
                    _ => {}
                }
            }
        }
    }
    out
}

/// Runtime checks for the invariants the compiler proved.
///
/// A proof is a claim about every execution; a demo is one execution. Making
/// the demo ASSERT what was proven turns every run — including every chaos
/// run — into a test of the prover itself. A proof unsoundness then shows up
/// as a failed assertion in a program the compiler blessed, rather than as a
/// number a human has to notice is wrong.
#[derive(Default)]
struct HoldChecks {
    /// (process, state, cmp, bound) — checked per shard.
    scalar: Vec<(String, String, String, i64)>,
    /// (process, lo_state, cmp, hi_state) — same process, checked per shard.
    relational: Vec<(String, String, String, String)>,
    /// (lo_proc, lo_state, cmp, hi_proc, hi_state) — checked on aggregates.
    system: Vec<(String, String, String, String, String)>,
}

fn cmp_str(op: &BinOp) -> Option<&'static str> {
    match op {
        BinOp::Le => Some("<="),
        BinOp::Lt => Some("<"),
        BinOp::Ge => Some(">="),
        BinOp::Gt => Some(">"),
        _ => None,
    }
}

fn collect_hold_checks(program: &Program) -> HoldChecks {
    use crate::frontend::ast::{Literal, SpecItem};
    let mut hc = HoldChecks::default();
    let is_proc = |n: &str| program.processes.iter().any(|p| p.name == n);
    let owner_of = |state: &str| -> Option<(String, Type)> {
        program.processes.iter().find_map(|process| {
            process
                .states
                .iter()
                .find(|candidate| candidate.name == state)
                .map(|decl| (process.name.clone(), decl.ty.clone()))
        })
    };
    let state_ty = |process: &str, state: &str| -> Option<Type> {
        program
            .processes
            .iter()
            .find(|candidate| candidate.name == process)?
            .states
            .iter()
            .find(|candidate| candidate.name == state)
            .map(|decl| decl.ty.clone())
    };

    for spec in &program.specs {
        for item in &spec.items {
            let SpecItem::Hold { expr, .. } = item else {
                continue;
            };
            let Expr::Binary { op, lhs, rhs, .. } = expr else {
                continue;
            };
            let Some(cmp) = cmp_str(op) else { continue };
            match (lhs.as_ref(), rhs.as_ref()) {
                // hold <state> <cmp> <literal>
                (Expr::Ident { name, .. }, Expr::Literal { value, .. }) => {
                    let bound = match value {
                        Literal::Int(i) => *i,
                        _ => continue,
                    };
                    if let Some((owner, Type::Int)) = owner_of(name) {
                        hc.scalar
                            .push((owner, name.clone(), cmp.to_string(), bound));
                    }
                }
                // hold <state> <cmp> <state>   (same process)
                (Expr::Ident { name: a, .. }, Expr::Ident { name: b, .. }) => {
                    if let (Some((oa, Type::Int)), Some((ob, Type::Int))) =
                        (owner_of(a), owner_of(b))
                    {
                        if oa == ob {
                            hc.relational
                                .push((oa, a.clone(), cmp.to_string(), b.clone()));
                        }
                    }
                }
                // hold P.x <cmp> Q.y
                (
                    Expr::FieldAccess {
                        base: lb,
                        field: lf,
                        ..
                    },
                    Expr::FieldAccess {
                        base: rb,
                        field: rf,
                        ..
                    },
                ) if is_proc(lb)
                    && is_proc(rb)
                    && matches!(state_ty(lb, lf), Some(Type::Int))
                    && matches!(state_ty(rb, rf), Some(Type::Int)) =>
                {
                    hc.system.push((
                        lb.clone(),
                        lf.clone(),
                        cmp.to_string(),
                        rb.clone(),
                        rf.clone(),
                    ));
                }
                _ => {}
            }
        }
    }
    hc
}

/// Multi-stage demo: spawn each process as a shard fleet in reverse
/// topological order, wire outboxes, feed the entry stage from concurrent
/// producers, then shut down stage by stage and print per-stage accounting.
fn emit_topology_main(program: &Program, topo: &crate::analysis::topology::Topology) -> String {
    use std::fmt::Write as _;
    let checks = collect_hold_checks(program);
    let entry_name = topo
        .order
        .first()
        .cloned()
        .unwrap_or_else(|| program.processes[0].name.clone());
    let entry = program
        .processes
        .iter()
        .find(|p| p.name == entry_name)
        .expect("entry process exists");
    let entry_handler = entry.handlers.first().expect("entry handler");

    // Message field synthesis (same policy as the fleet demo).
    let mut field_init = String::new();
    if let Type::Named(name) = &entry_handler.msg_ty {
        if let Some(schema) = program.schemas.iter().find(|sc| sc.name == *name) {
            for (fname, fty) in &schema.fields {
                match fty {
                    Type::Int => {
                        let _ = writeln!(field_init, "                msg.{fname} = 1;");
                    }
                    Type::Float => {
                        let _ = writeln!(field_init, "                msg.{fname} = 1.0;");
                    }
                    Type::String => {
                        let _ = writeln!(
                            field_init,
                            "                msg.{fname} = format!(\"m-{{prod}}-{{i}}\");"
                        );
                    }
                    Type::Bool => {
                        let _ = writeln!(field_init, "                msg.{fname} = true;");
                    }
                    _ => {}
                }
            }
        }
    }

    let edge_list = topo
        .edges
        .iter()
        .map(|e| format!("{} -> {} [{}]", e.from, e.to, e.msg_type))
        .collect::<Vec<_>>()
        .join(", ");

    let mut out = String::new();
    let _ = writeln!(
        out,
        "// AUTO-GENERATED by sigilc — multi-stage topology demo"
    );
    let _ = write!(out, "//\n// Topology: {edge_list}\n");
    let _ = writeln!(
        out,
        "// Each stage is a fleet of shared-nothing actors; the compiler wired the"
    );
    let _ = writeln!(
        out,
        "// outboxes, verified message types along every edge, proved the graph"
    );
    let _ = write!(out, "// acyclic, and staged the shutdown so no message is ever lost in a closed\n// channel. Sigil emits no explicit locks or shared mutable state.\n\n");
    out.push_str("use sigil_gen::*;\nuse std::time::Instant;\n\n");
    out.push_str(
        "#[tokio::main(flavor = \"multi_thread\")]\nasync fn main() -> sigil_rt::Result<()> {\n",
    );
    out.push_str("    fn env_usize(key: &str, default: usize) -> usize {\n        std::env::var(key).ok().and_then(|v| v.parse().ok()).unwrap_or(default)\n    }\n");
    out.push_str("    let shards: usize = env_usize(\"SIGIL_DEMO_SHARDS\", 8);\n");
    out.push_str("    let producers: usize = env_usize(\"SIGIL_DEMO_PRODUCERS\", 64);\n");
    out.push_str("    let msgs_per_producer: usize = env_usize(\"SIGIL_DEMO_MSGS\", 250);\n");
    out.push_str("    let capacity: usize = env_usize(\"SIGIL_DEMO_CAPACITY\", 1024);\n");
    out.push_str(
        "    if shards == 0 {\n        return Err(sigil_rt::SigilError::Configuration(\"SIGIL_DEMO_SHARDS must be at least 1\".into()));\n    }\n",
    );
    out.push_str(
        "    let total = producers.checked_mul(msgs_per_producer).ok_or_else(|| sigil_rt::SigilError::Configuration(\"demo message count overflows usize\".into()))?;\n\n",
    );

    // Spawn sinks first so upstream stages can be wired to live handles.
    for pname in topo.order.iter().rev() {
        let lc = pname.to_lowercase();
        let _ = writeln!(out, "    // stage: {pname}");
        let _ = write!(
            out,
            "    let mut {lc}_handles = Vec::new();\n    let mut {lc}_joins = Vec::new();\n"
        );
        let _ = writeln!(out, "    for _i in 0..shards {{");
        let targets = topo.targets_of(pname);
        if targets.is_empty() {
            let _ = writeln!(out, "        let inst = {pname}::new();");
        } else {
            let _ = writeln!(out, "        let mut inst = {pname}::new();");
            for e in &targets {
                let tlc = e.to.to_lowercase();
                let _ = writeln!(out, "        inst.connect_{tlc}({tlc}_handles.clone())?;");
            }
        }
        let _ = write!(out, "        let (h, j) = inst.spawn(capacity)?;\n        {lc}_handles.push(h);\n        {lc}_joins.push(j);\n    }}\n\n");
    }

    let entry_lc = entry_name.to_lowercase();
    let _ = writeln!(out, "    let t0 = Instant::now();");
    let _ = writeln!(out, "    let mut producer_tasks = Vec::new();");
    let multi_entry = entry.handlers.len() > 1;
    // Producers cycle through EVERY handler of the entry process, so a
    // multi-handler entry is genuinely exercised (and the totals stay exact).
    let mut feed_arms = String::new();
    for (k, hh) in entry.handlers.iter().enumerate() {
        let ty = rust_type(&hh.msg_ty);
        let init = demo_field_init(program, &hh.msg_ty, "msg", "                        ");
        let method = if multi_entry {
            format!("send_{}", hh.msg_name)
        } else {
            "send".to_string()
        };
        let _ = write!(
            feed_arms,
            "                    {k} => {{\n                        let mut msg = {ty}::default();\n{init}                        hs[shard].{method}(msg).await?;\n                    }}\n"
        );
    }
    let n_handlers = entry.handlers.len();
    let _ = write!(
        out,
        "    for prod in 0..producers {{\n\
        \x20       let hs = {entry_lc}_handles.clone();\n\
        \x20       producer_tasks.push(tokio::spawn(async move {{\n\
        \x20           for i in 0..msgs_per_producer {{\n\
        \x20               let shard = (prod + i) % hs.len();\n\
        \x20               match (prod + i) % {n_handlers} {{\n\
{feed_arms}\
        \x20                   _ => return Err(sigil_rt::SigilError::Configuration(\"invalid generated handler dispatch\".into())),\n\
        \x20               }}\n\
        \x20           }}\n\
        \x20           Ok::<(), sigil_rt::SigilError>(())\n\
        \x20       }}));\n\
        \x20   }}\n\
        \x20   for t in producer_tasks {{\n\
        \x20       sigil_rt::join_task(t).await??;\n\
        \x20   }}\n\n"
    );

    let _ = writeln!(out, "    println!(\"=== topology demo: {edge_list} ===\");");
    out.push_str("    println!(\"{} shards/stage, {} producers, {} messages, {} worker threads\", shards, producers, total, std::thread::available_parallelism().map(|n| n.get()).unwrap_or(1));\n");

    // Shut down and report stage by stage in topological order.
    for pname in &topo.order {
        let lc = pname.to_lowercase();
        let process = program.processes.iter().find(|p| &p.name == pname).unwrap();
        let mut shard_fmt = String::new();
        let mut shard_args = String::new();
        let mut agg_decl = String::new();
        let mut agg_add = String::new();
        let mut agg_print = String::new();
        for st in &process.states {
            match st.ty {
                Type::Int => {
                    let _ = writeln!(agg_decl, "    let mut {lc}_agg_{}: i128 = 0;", st.name);
                    let _ = writeln!(
                        agg_add,
                        "        {lc}_agg_{} += i128::from(st.{});",
                        st.name, st.name
                    );
                    let _ = writeln!(
                        agg_print,
                        "    println!(\"[{pname}] aggregate {} = {{}}\", {lc}_agg_{});",
                        st.name, st.name
                    );
                    let _ = write!(shard_fmt, " {}={{}}", st.name);
                    let _ = write!(shard_args, ", st.{}", st.name);
                }
                Type::Float => {
                    let _ = writeln!(agg_decl, "    let mut {lc}_agg_{}: f64 = 0.0;", st.name);
                    let _ = writeln!(agg_add, "        {lc}_agg_{} += st.{};", st.name, st.name);
                    let _ = writeln!(
                        agg_print,
                        "    println!(\"[{pname}] aggregate {} = {{:.1}}\", {lc}_agg_{});",
                        st.name, st.name
                    );
                    let _ = write!(shard_fmt, " {}={{:.1}}", st.name);
                    let _ = write!(shard_args, ", st.{}", st.name);
                }
                _ => {}
            }
        }
        // Per-shard checks of everything proven about THIS process.
        let mut shard_asserts = String::new();
        for (proc_name, state, cmp, bound) in &checks.scalar {
            if proc_name != pname {
                continue;
            }
            let _ = writeln!(
                shard_asserts,
                "        assert!(st.{state} {cmp} {bound}i64, \"PROVEN INVARIANT VIOLATED: {pname}.{state} {cmp} {bound} (shard {{idx}}, got {{}})\", st.{state});"
            );
        }
        for (proc_name, a, cmp, b) in &checks.relational {
            if proc_name != pname {
                continue;
            }
            let _ = writeln!(
                shard_asserts,
                "        assert!(st.{a} {cmp} st.{b}, \"PROVEN INVARIANT VIOLATED: {pname}.{a} {cmp} {pname}.{b} (shard {{idx}}, {{}} vs {{}})\", st.{a}, st.{b});"
            );
        }

        let _ = write!(out, "\n    // ---- stage shutdown: {pname} ----\n");
        let _ = writeln!(out, "    drop({lc}_handles);");
        let _ = write!(out, "{agg_decl}");
        let _ = write!(out, "    let mut {lc}_handled: u64 = 0;\n    let mut {lc}_dropped: u64 = 0;\n    let mut {lc}_shed: u64 = 0;\n");
        let _ = write!(out, "    for (idx, j) in {lc}_joins.into_iter().enumerate() {{\n        let (st, stats) = sigil_rt::join_actor(j).await?;\n        println!(\"[{pname}] shard {{idx}}:{shard_fmt} handled={{}} dropped={{}}\"{shard_args}, stats.handled, stats.dropped);\n{agg_add}{shard_asserts}        {lc}_handled += stats.handled;\n        {lc}_dropped += stats.dropped;\n        {lc}_shed += stats.shed;\n    }}\n");
        let _ = write!(out, "{agg_print}");
        let _ = writeln!(out, "    println!(\"[{pname}] handled + dropped = {{}} + {{}} = {{}}, shed downstream = {{}}\", {lc}_handled, {lc}_dropped, {lc}_handled + {lc}_dropped, {lc}_shed);");
    }

    // System invariants relate different processes, whose shards do not
    // correspond (routing differs), so they are checked on the aggregates.
    // Summing a per-shard inequality preserves it.
    if !checks.system.is_empty() {
        let _ = write!(
            out,
            "\n    // ---- proven system invariants, checked at runtime ----\n"
        );
        for (lp, ls, cmp, hp, hs) in &checks.system {
            let (llc, hlc) = (lp.to_lowercase(), hp.to_lowercase());
            let _ = writeln!(
                out,
                "    assert!({llc}_agg_{ls} {cmp} {hlc}_agg_{hs}, \"PROVEN INVARIANT VIOLATED: {lp}.{ls} {cmp} {hp}.{hs} ({{}} vs {{}})\", {llc}_agg_{ls}, {hlc}_agg_{hs});"
            );
        }
        let _ = writeln!(
            out,
            "    println!(\"all {} proven invariant(s) verified at runtime\");",
            checks.system.len() + checks.scalar.len() + checks.relational.len()
        );
    }

    let _ = write!(out, "\n    assert_eq!({entry_lc}_handled + {entry_lc}_dropped, total as u64, \"entry-stage message conservation violated\");\n");
    let _ = writeln!(out, "    println!(\"expected messages   = {{total}}\");");
    let _ = writeln!(
        out,
        "    println!(\"elapsed = {{:?}} (locks emitted by Sigil: 0)\", t0.elapsed());"
    );
    let _ = write!(
        out,
        "    println!(\"{{}}\", sigil_rt::chaos::report());\n    Ok(())\n}}\n"
    );
    out
}

pub fn emit_cargo_toml_with_deps(
    package_name: &str,
    sigil_rt_path: &str,
    with_main: bool,
    externs: &[crate::frontend::ast::ExternCrate],
) -> String {
    use crate::frontend::ast::CrateSource;
    fn toml_string(value: &str) -> String {
        let mut escaped = String::with_capacity(value.len() + 2);
        escaped.push('"');
        for ch in value.chars() {
            match ch {
                '\\' => escaped.push_str("\\\\"),
                '"' => escaped.push_str("\\\""),
                '\n' => escaped.push_str("\\n"),
                '\r' => escaped.push_str("\\r"),
                '\t' => escaped.push_str("\\t"),
                ch if ch.is_control() => {
                    use std::fmt::Write as _;
                    let _ = write!(escaped, "\\u{:04X}", ch as u32);
                }
                ch => escaped.push(ch),
            }
        }
        escaped.push('"');
        escaped
    }

    let base = emit_cargo_toml(package_name, sigil_rt_path, with_main);
    if externs.is_empty() {
        return base;
    }
    let mut extra = String::from("\n# Declared in the .sigil source via `extern crate`.\n");
    for e in externs {
        match &e.source {
            CrateSource::Version(v) => {
                extra.push_str(&format!("{} = {}\n", e.name, toml_string(v)));
            }
            CrateSource::Path(p) => {
                extra.push_str(&format!("{} = {{ path = {} }}\n", e.name, toml_string(p)));
            }
        }
    }
    // Insert at the end of the [dependencies] table. `thiserror` is always
    // the last unconditional entry, so anchor on it rather than on optional
    // sections that may or may not be present.
    let anchor = "thiserror = \"1\"\n";
    match base.find(anchor) {
        Some(i) => {
            let at = i + anchor.len();
            format!(
                "{}{}{}",
                &base[..at],
                extra.trim_start_matches('\n'),
                &base[at..]
            )
        }
        None => format!("{base}{extra}"),
    }
}

pub fn emit_cargo_toml(package_name: &str, sigil_rt_path: &str, with_main: bool) -> String {
    fn cargo_package_name(name: &str) -> String {
        let sanitized: String = name
            .chars()
            .map(|ch| {
                if ch.is_ascii_alphanumeric() || matches!(ch, '-' | '_') {
                    ch
                } else {
                    '_'
                }
            })
            .collect();
        if sanitized.is_empty() || !sanitized.chars().any(|ch| ch.is_ascii_alphanumeric()) {
            "sigil_out".into()
        } else {
            sanitized
        }
    }

    let package_name = cargo_package_name(package_name);
    let sigil_rt_path = sigil_rt_path
        .replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
        .replace('\t', "\\t");
    let bin_section = if with_main {
        "\n[[bin]]\nname = \"demo\"\npath = \"src/main.rs\"\n"
    } else {
        ""
    };
    format!(
        "[workspace]\n\n# Integer overflow must never wrap silently: a wrapped counter would\n# invalidate invariants the compiler proved. Enabled in ALL profiles.\n[profile.release]\noverflow-checks = true\n\n[profile.dev]\noverflow-checks = true\n\n[package]\nname = \"{package_name}\"\nversion = \"0.1.0\"\nedition = \"2021\"\n\n[dependencies]\ntokio = {{ version = \"1\", features = [\"time\", \"macros\", \"rt-multi-thread\", \"sync\"] }}\nsigil_rt = {{ path = \"{sigil_rt_path}\" }}\nthiserror = \"1\"\ntracing = {{ version = \"0.1\", optional = true }}\n\n[features]\ndefault = []\n# Emits a tracing span per handler invocation. Off by default so the\n# generated crate adds no dependency you did not ask for.\ntracing = [\"dep:tracing\"]\n\n[lib]\nname = \"sigil_gen\"\npath = \"src/lib.rs\"\n{bin_section}"
    )
}

/// Emit a runnable main that stress-tests the process as a fleet of
/// shared-nothing actors: SHARDS isolated instances, PRODUCERS concurrent
/// tasks on a multi-threaded runtime, deterministic aggregate at the end.
/// No explicit locks appear in the code emitted by Sigil.
pub fn emit_demo_main(program: &Program) -> String {
    if program.processes.is_empty() {
        return "fn main() {\n    println!(\"no process\");\n}\n".into();
    }
    if let Ok(topo) = crate::analysis::topology::derive_topology(program) {
        if topo.is_pipeline() {
            return emit_topology_main(program, &topo);
        }
    }
    let process = &program.processes[0];
    let p = &process.name;
    let Some(handler) = process.handlers.first() else {
        return "fn main() {\n    println!(\"no handler\");\n}\n".into();
    };
    let msg_ty = rust_type(&handler.msg_ty);

    // Synthesize per-message field values so aggregates are checkable:
    // Int fields = 1, Float fields = 1.0, Strings get unique ids.
    let mut field_init = String::new();
    if let Type::Named(name) = &handler.msg_ty {
        if let Some(schema) = program.schemas.iter().find(|sc| sc.name == *name) {
            for (fname, fty) in &schema.fields {
                match fty {
                    Type::Int => {
                        field_init.push_str(&format!("                msg.{fname} = 1;\n"))
                    }
                    Type::Float => {
                        field_init.push_str(&format!("                msg.{fname} = 1.0;\n"))
                    }
                    Type::String => field_init.push_str(&format!(
                        "                msg.{fname} = format!(\"m-{{prod}}-{{i}}\");\n"
                    )),
                    Type::Bool => {
                        field_init.push_str(&format!("                msg.{fname} = true;\n"))
                    }
                    _ => {}
                }
            }
        }
    }

    // Runtime checks of everything proven about this process, applied per
    // shard as its state comes back from join().
    let checks = collect_hold_checks(program);
    let mut fleet_asserts = String::new();
    {
        use std::fmt::Write as _;
        for (proc_name, state, cmp, bound) in &checks.scalar {
            if proc_name != &process.name {
                continue;
            }
            let _ = writeln!(
                fleet_asserts,
                "        assert!(st.{state} {cmp} {bound}i64, \"PROVEN INVARIANT VIOLATED: {p}.{state} {cmp} {bound} (shard {{idx}}, got {{}})\", st.{state});"
            );
        }
        for (proc_name, a, cmp, b) in &checks.relational {
            if proc_name != &process.name {
                continue;
            }
            let _ = writeln!(
                fleet_asserts,
                "        assert!(st.{a} {cmp} st.{b}, \"PROVEN INVARIANT VIOLATED: {p}.{a} {cmp} {p}.{b} (shard {{idx}}, {{}} vs {{}})\", st.{a}, st.{b});"
            );
        }
    }

    // Aggregate numeric states across shards.
    let mut agg_decl = String::new();
    let mut agg_add = String::new();
    let mut agg_print = String::new();
    let mut shard_print_fmt = String::new();
    let mut shard_print_args = String::new();
    for st in &process.states {
        match st.ty {
            Type::Int => {
                agg_decl.push_str(&format!("    let mut agg_{}: i128 = 0;\n", st.name));
                agg_add.push_str(&format!(
                    "        agg_{} += i128::from(st.{});\n",
                    st.name, st.name
                ));
                agg_print.push_str(&format!(
                    "    println!(\"aggregate {} = {{}}\", agg_{});\n",
                    st.name, st.name
                ));
                shard_print_fmt.push_str(&format!(" {}={{}}", st.name));
                shard_print_args.push_str(&format!(", st.{}", st.name));
            }
            Type::Float => {
                agg_decl.push_str(&format!("    let mut agg_{}: f64 = 0.0;\n", st.name));
                agg_add.push_str(&format!("        agg_{} += st.{};\n", st.name, st.name));
                agg_print.push_str(&format!(
                    "    println!(\"aggregate {} = {{:.1}}\", agg_{});\n",
                    st.name, st.name
                ));
                shard_print_fmt.push_str(&format!(" {}={{:.1}}", st.name));
                shard_print_args.push_str(&format!(", st.{}", st.name));
            }
            _ => {
                shard_print_fmt.push_str(&format!(" {}={{:?}}", st.name));
                shard_print_args.push_str(&format!(", st.{}", st.name));
            }
        }
    }

    let lib_name = "{{LIB}}"; // patched by caller via crate name? No: use crate path.
    let _ = lib_name;

    format!(
        r#"// AUTO-GENERATED by sigilc — concurrent demo binary
//
// {shards} isolated `{p}` actors, {producers} producer tasks, {msgs} messages
// on a multi-threaded runtime. Every counter below is exact — there is no
// lock, no Arc, and no atomic in this program. State isolation is by
// construction: each actor owns its state inside its own task.

use sigil_gen::*;
use std::time::Instant;

#[tokio::main(flavor = "multi_thread")]
async fn main() -> sigil_rt::Result<()> {{
    fn env_usize(key: &str, default: usize) -> usize {{
        std::env::var(key).ok().and_then(|v| v.parse().ok()).unwrap_or(default)
    }}
    let shards: usize = env_usize("SIGIL_DEMO_SHARDS", {shards});
    let producers: usize = env_usize("SIGIL_DEMO_PRODUCERS", {producers});
    let msgs_per_producer: usize = env_usize("SIGIL_DEMO_MSGS", {per_producer});
    let capacity: usize = env_usize("SIGIL_DEMO_CAPACITY", 1024);
    if shards == 0 {{
        return Err(sigil_rt::SigilError::Configuration(
            "SIGIL_DEMO_SHARDS must be at least 1".into(),
        ));
    }}
    let total = producers.checked_mul(msgs_per_producer).ok_or_else(|| {{
        sigil_rt::SigilError::Configuration("demo message count overflows usize".into())
    }})?;

    let mut handles = Vec::new();
    let mut joins = Vec::new();
    for _ in 0..shards {{
        let (h, j) = {p}::new().spawn(capacity)?;
        handles.push(h);
        joins.push(j);
    }}

    let t0 = Instant::now();
    let mut producer_tasks = Vec::new();
    for prod in 0..producers {{
        let hs = handles.clone();
        producer_tasks.push(tokio::spawn(async move {{
            for i in 0..msgs_per_producer {{
                let mut msg = {msg_ty}::default();
{field_init}                let shard = (prod + i) % hs.len();
                hs[shard].send(msg).await?;
            }}
            Ok::<(), sigil_rt::SigilError>(())
        }}));
    }}
    for t in producer_tasks {{
        sigil_rt::join_task(t).await??;
    }}
    drop(handles); // close all channels → actors drain and return their state

    println!("=== {p} concurrent demo ===");
    println!(
        "{{}} shards x {{}} producers, {{}} messages, {{}} worker threads",
        shards,
        producers,
        total,
        std::thread::available_parallelism().map(|n| n.get()).unwrap_or(1)
    );
{agg_decl}    let mut agg_dropped: u64 = 0;
    let mut agg_handled: u64 = 0;
    for (idx, j) in joins.into_iter().enumerate() {{
        let (st, stats) = sigil_rt::join_actor(j).await?;
        println!("shard {{idx}}:{shard_print_fmt} handled={{}} dropped={{}}"{shard_print_args}, stats.handled, stats.dropped);
{fleet_asserts}{agg_add}        agg_handled += stats.handled;
        agg_dropped += stats.dropped;
    }}
{agg_print}    println!("handled + dropped   = {{}} + {{}} = {{}}", agg_handled, agg_dropped, agg_handled + agg_dropped);
    println!("expected messages   = {{total}}");
    assert_eq!(
        agg_handled + agg_dropped,
        total as u64,
        "message conservation violated"
    );
    println!("elapsed = {{:?}} (locks emitted by Sigil: 0)", t0.elapsed());
    println!("{{}}", sigil_rt::chaos::report());
    Ok(())
}}
"#,
        shards = 8,
        producers = 64,
        per_producer = 250,
        msgs = 64 * 250,
    )
}

/// Compute a relative path from `out_dir` to the workspace `sigil_rt` crate.
pub fn relative_sigil_rt_path(out_dir: &std::path::Path) -> String {
    use std::path::{Path, PathBuf};

    let out_abs = if out_dir.is_absolute() {
        out_dir.to_path_buf()
    } else {
        std::env::current_dir()
            .unwrap_or_else(|_| PathBuf::from("."))
            .join(out_dir)
    };

    let mut candidates = Vec::new();
    if let Ok(current) = std::env::current_dir() {
        for ancestor in current.ancestors() {
            candidates.push(ancestor.join("sigil_rt"));
        }
    }
    for ancestor in out_abs.ancestors() {
        candidates.push(ancestor.join("sigil_rt"));
    }
    if let Some(workspace) = Path::new(env!("CARGO_MANIFEST_DIR")).parent() {
        candidates.push(workspace.join("sigil_rt"));
    }

    for runtime in candidates {
        if !runtime.join("Cargo.toml").is_file() {
            continue;
        }
        if let Some(relative) = pathdiff::diff_paths(&runtime, &out_abs) {
            return relative.to_string_lossy().replace('\\', "/");
        }
    }

    // Keep the failure visible in the generated manifest. Cargo will report
    // the missing runtime path instead of silently selecting another crate.
    "../sigil_rt".into()
}

#[cfg(test)]
mod cargo_manifest_tests {
    use super::*;

    #[test]
    fn manifest_values_cannot_inject_toml() {
        let manifest = emit_cargo_toml("bad\"\nowned = true", "bad\"\npath", false);
        assert!(!manifest.contains("\nowned = true"));
        assert!(manifest.contains("name = \"bad__owned___true\""));
        assert!(manifest.contains("path = \"bad\\\"\\npath\""));
    }

    #[test]
    fn runtime_path_resolution_is_not_limited_by_directory_depth() {
        let deep = std::env::current_dir()
            .expect("current directory")
            .join("target/a/b/c/generated");
        let relative = relative_sigil_rt_path(&deep);
        let workspace = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
            .parent()
            .expect("workspace root");
        let expected =
            pathdiff::diff_paths(workspace.join("sigil_rt"), &deep).expect("relative path");
        assert_eq!(relative, expected.to_string_lossy().replace('\\', "/"));
    }
}
