//! Backend: Program + Graph IR → ownership-safe Rust crate.
//! Level-1 guarantees are preserved by construction in the emitted code.

use crate::analysis::ir::GraphIR;
use crate::frontend::ast::{
    BinOp, Expr, Literal, OnHandler, Process, Program, Schema, Stmt, Tag, Type,
};
use std::collections::BTreeSet;

/// Emit a full Rust library source from the checked program and its Graph IR.
pub fn emit(program: &Program, ir: &GraphIR) -> String {
    let mut out = String::new();
    out.push_str("// AUTO-GENERATED by sigilc — do not edit by hand\n");
    out.push_str("//\n");
    out.push_str(&format!("// Process: {}\n", ir.process_name));
    out.push_str(&format!(
        "// Local state: {}\n",
        if ir.local_states.is_empty() {
            "(none)".into()
        } else {
            ir.local_states.join(", ")
        }
    ));
    let timeouts: Vec<_> = ir
        .nodes
        .iter()
        .filter_map(|n| match n {
            crate::analysis::ir::Node::Timeout { ms, .. } => Some(format!("{ms}ms")),
            _ => None,
        })
        .collect();
    if !timeouts.is_empty() {
        out.push_str(&format!("// Timed stages: {}\n", timeouts.join(", ")));
    }
    out.push_str("//\n");
    out.push_str("// Level-1 guarantees preserved by construction:\n");
    out.push_str("//   - process-local state only (no shared mutability)\n");
    out.push_str("//   - no null values\n");
    out.push_str("//   - every @timeout has an explicit @recover path\n\n");

    out.push_str("use std::time::Duration;\n");
    out.push_str("use tokio::time::timeout;\n");
    out.push_str("use sigil_rt::Result;\n\n");

    for schema in &program.schemas {
        out.push_str(&emit_schema(schema));
        out.push('\n');
    }

    for process in &program.processes {
        out.push_str(&emit_process(process));
        out.push('\n');
        out.push_str(&emit_actor(process));
        out.push('\n');
    }

    let primary_ty = primary_message_type(program);
    let (_env, transform_types) = crate::analysis::types::infer_program(program);

    // Prefer declared transforms: pure bodies compile; empty bodies are residual stubs.
    let mut emitted: BTreeSet<String> = BTreeSet::new();
    if !program.transforms.is_empty() {
        out.push_str("// --- declared transforms ---\n");
        for t in &program.transforms {
            out.push_str(&emit_declared_transform(t));
            out.push('\n');
            emitted.insert(t.name.clone());
        }
    }

    let externals = collect_externals(program);
    let leftover: Vec<_> = externals.difference(&emitted).cloned().collect();
    if !leftover.is_empty() {
        out.push_str("// --- undeclared transforms (residual stubs) ---\n");
        for name in leftover {
            let (in_ty, out_ty) = transform_types
                .get(&name)
                .cloned()
                .unwrap_or_else(|| (primary_ty.clone(), primary_ty.clone()));
            out.push_str(&emit_external_stub(&name, &in_ty, &out_ty));
            out.push('\n');
        }
    }

    out.push_str(&emit_smoke_test(program));
    out
}

fn emit_declared_transform(t: &crate::frontend::ast::TransformDecl) -> String {
    let in_ty = rust_type(&t.param_ty);
    let out_ty = rust_type(&t.return_ty);
    let param = &t.param;
    if t.body.is_empty() {
        return emit_external_stub(&t.name, &in_ty, &out_ty);
    }
    let mut body_src = String::new();
    for stmt in &t.body {
        match stmt {
            Stmt::Let { name, expr, .. } => {
                body_src.push_str(&format!(
                    "    let {name} = {};\n",
                    emit_expr(expr, &[], param)
                ));
            }
            Stmt::Expr { expr, .. } => {
                body_src.push_str(&format!(
                    "    return Ok({});\n",
                    emit_expr(expr, &[], param)
                ));
            }
            Stmt::Assign { .. } => {
                body_src.push_str("    /* unsupported assign in transform */\n");
            }
            Stmt::Send { .. } => {
                body_src.push_str("    /* send is only legal in process handlers */\n");
            }
        }
    }
    if !body_src.contains("return Ok") {
        body_src.push_str(&format!("    Ok({param})\n"));
    }
    format!(
        "async fn {}({param}: {in_ty}) -> Result<{out_ty}> {{\n{body_src}}}\n",
        t.name
    )
}

fn primary_message_type(program: &Program) -> String {
    program
        .processes
        .first()
        .and_then(|p| p.handlers.first())
        .map(|h| rust_type(&h.msg_ty))
        .unwrap_or_else(|| "()".into())
}

fn emit_schema(schema: &Schema) -> String {
    let mut s = String::new();
    s.push_str("#[derive(Clone, Debug, Default)]\n");
    s.push_str(&format!("pub struct {} {{\n", schema.name));
    for (fname, fty) in &schema.fields {
        s.push_str(&format!("    pub {}: {},\n", fname, rust_type(fty)));
    }
    s.push_str("}\n");
    s
}


/// Processes this process sends to, in declaration order (deduped).
fn process_send_targets(process: &Process) -> Vec<String> {
    let mut targets: Vec<String> = Vec::new();
    for handler in &process.handlers {
        for stmt in &handler.body {
            if let Stmt::Send { target, .. } = stmt {
                if !targets.contains(target) {
                    targets.push(target.clone());
                }
            }
        }
    }
    targets
}

fn emit_process(process: &Process) -> String {
    let states: Vec<String> = process.states.iter().map(|s| s.name.clone()).collect();
    let mut s = String::new();

    let targets = process_send_targets(process);
    s.push_str(&format!("pub struct {} {{\n", process.name));
    for st in &process.states {
        // Public so integration tests / callers can observe local state.
        s.push_str(&format!("    pub {}: {},\n", st.name, rust_type(&st.ty)));
    }
    for t in &targets {
        s.push_str(&format!(
            "    /// Outbox to the {t} actor. Wire with connect_{}() before spawn.\n",
            t.to_lowercase()
        ));
        s.push_str(&format!(
            "    pub {}_out: Option<sigil_rt::Router<{t}Handle>>,\n",
            t.to_lowercase()
        ));
    }
    s.push_str("}\n\n");

    s.push_str(&format!("impl {} {{\n", process.name));
    s.push_str("    pub fn new() -> Self {\n        Self {\n");
    for st in &process.states {
        s.push_str(&format!(
            "            {}: {},\n",
            st.name,
            emit_expr(&st.init, &[], "")
        ));
    }
    for t in &targets {
        s.push_str(&format!("            {}_out: None,\n", t.to_lowercase()));
    }
    s.push_str("        }\n    }\n\n");
    for t in &targets {
        s.push_str(&format!(
            "    pub fn connect_{}(&mut self, shards: Vec<{t}Handle>) {{\n        self.{}_out = Some(sigil_rt::Router::new(shards));\n    }}\n\n",
            t.to_lowercase(),
            t.to_lowercase()
        ));
    }

    for handler in &process.handlers {
        s.push_str(&emit_handler(handler, &states));
        s.push('\n');
    }
    s.push_str("}\n");
    s
}


/// Emit shared-nothing actor machinery for a process.
///
/// This is where the Level-1 "no shared mutable state" guarantee is realized
/// in the generated Rust, not just checked by the compiler:
///   - `spawn(self)` MOVES the state into an isolated tokio task; after that
///     no reference to it can exist anywhere else in the program.
///   - The only way in is a message via a Clone-able `Handle` (an mpsc Sender).
///   - The only way out is `join()` after the channel closes.
/// There is no `Arc`, no `Mutex`, and no way to observe state concurrently.
fn emit_actor(process: &Process) -> String {
    if process.handlers.is_empty() {
        return String::new();
    }
    let p = &process.name;
    let mut s = String::new();

    let drop_outboxes: String = process_send_targets(process)
        .iter()
        .map(|t| format!("            self.{}_out = None; // release downstream channel\n", t.to_lowercase()))
        .collect();
    if process.handlers.len() == 1 {
        let h = &process.handlers[0];
        let msg_ty = rust_type(&h.msg_ty);
        let method = format!("on_{}", h.msg_name);
        s.push_str(&format!(
            "/// Clone-able, thread-safe handle to a running `{p}` actor.\n\
             /// Sending is the ONLY way to interact with the actor's state.\n\
             #[derive(Clone)]\n\
             pub struct {p}Handle {{\n\
             \x20   tx: tokio::sync::mpsc::Sender<{msg_ty}>,\n\
             }}\n\n\
             impl {p}Handle {{\n\
             \x20   pub async fn send(&self, msg: {msg_ty}) -> Result<()> {{\n\
             \x20       self.tx\n\
             \x20           .send(msg)\n\
             \x20           .await\n\
             \x20           .map_err(|_| sigil_rt::SigilError::Transform(\"actor stopped\".into()))\n\
             \x20   }}\n\
             }}\n\n\
             impl {p} {{\n\
             \x20   /// Move this process's state into an isolated task.\n\
             \x20   /// After `spawn`, the state is unreachable except via messages;\n\
             \x20   /// it is returned intact when the last Handle is dropped.\n\
             \x20   pub fn spawn(mut self, capacity: usize) -> ({p}Handle, tokio::task::JoinHandle<(Self, sigil_rt::ActorStats)>) {{\n\
             \x20       let (tx, mut rx) = tokio::sync::mpsc::channel::<{msg_ty}>(capacity);\n\
             \x20       let join = tokio::spawn(async move {{\n\
             \x20           let mut stats = sigil_rt::ActorStats::default();\n\
             \x20           while let Some(msg) = rx.recv().await {{\n\
             \x20               match self.{method}(msg).await {{\n\
             \x20                   Ok(()) => stats.handled += 1,\n\
             \x20                   Err(_) => stats.dropped += 1, // residual stage failed with no recover path\n\
             \x20               }}\n\
             \x20           }}\n\
{drop_outboxes}\
             \x20           (self, stats)\n\
             \x20       }});\n\
             \x20       ({p}Handle {{ tx }}, join)\n\
             \x20   }}\n\
             }}\n"
        ));
        return s;
    }

    // Multiple handlers: dispatch enum, one variant per message type.
    let enum_name = format!("{p}Msg");
    s.push_str(&format!("pub enum {enum_name} {{\n"));
    for h in &process.handlers {
        s.push_str(&format!(
            "    {}({}),\n",
            variant_name(&h.msg_name),
            rust_type(&h.msg_ty)
        ));
    }
    s.push_str("}\n\n");

    s.push_str(&format!(
        "#[derive(Clone)]\npub struct {p}Handle {{\n    tx: tokio::sync::mpsc::Sender<{enum_name}>,\n}}\n\nimpl {p}Handle {{\n"
    ));
    for h in &process.handlers {
        s.push_str(&format!(
            "    pub async fn send_{}(&self, msg: {}) -> Result<()> {{\n        self.tx.send({enum_name}::{}(msg)).await.map_err(|_| sigil_rt::SigilError::Transform(\"actor stopped\".into()))\n    }}\n",
            h.msg_name,
            rust_type(&h.msg_ty),
            variant_name(&h.msg_name)
        ));
    }
    s.push_str("}\n\n");

    s.push_str(&format!(
        "impl {p} {{\n    pub fn spawn(mut self, capacity: usize) -> ({p}Handle, tokio::task::JoinHandle<(Self, sigil_rt::ActorStats)>) {{\n        let (tx, mut rx) = tokio::sync::mpsc::channel::<{enum_name}>(capacity);\n        let join = tokio::spawn(async move {{\n            let mut stats = sigil_rt::ActorStats::default();\n            while let Some(msg) = rx.recv().await {{\n                let r = match msg {{\n"
    ));
    for h in &process.handlers {
        s.push_str(&format!(
            "                    {enum_name}::{}(m) => self.on_{}(m).await,\n",
            variant_name(&h.msg_name),
            h.msg_name
        ));
    }
    s.push_str(&format!(
        "                }};\n                match r {{\n                    Ok(()) => stats.handled += 1,\n                    Err(_) => stats.dropped += 1,\n                }}\n            }}\n{drop_outboxes}            (self, stats)\n        }});\n        ({p}Handle {{ tx }}, join)\n    }}\n}}\n"
    ));
    s
}

fn variant_name(msg_name: &str) -> String {
    let mut c = msg_name.chars();
    match c.next() {
        Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
        None => String::new(),
    }
}

fn emit_handler(handler: &OnHandler, states: &[String]) -> String {
    let msg_ty = rust_type(&handler.msg_ty);
    let method = format!("on_{}", handler.msg_name);
    let mut s = String::new();
    s.push_str(&format!(
        "    pub async fn {method}(&mut self, {}: {msg_ty}) -> Result<()> {{\n",
        handler.msg_name
    ));

    for stmt in &handler.body {
        s.push_str(&emit_stmt(stmt, "        ", states, &handler.msg_name));
    }

    s.push_str("        Ok(())\n");
    s.push_str("    }\n");
    s
}

fn emit_stmt(stmt: &Stmt, indent: &str, states: &[String], msg: &str) -> String {
    match stmt {
        Stmt::Let { name, expr, .. } => {
            // Prefer multi-line for timeout pipelines so the recover path is readable.
            if is_timed_pipeline(expr) {
                format!(
                    "{indent}let {name} = {};\n",
                    emit_expr_multiline(expr, states, msg, indent)
                )
            } else {
                format!("{indent}let {name} = {};\n", emit_expr(expr, states, msg))
            }
        }
        Stmt::Assign { name, expr, .. } => {
            format!(
                "{indent}self.{name} = {};\n",
                emit_expr(expr, states, msg)
            )
        }
        Stmt::Send { target, expr, route, .. } => {
            let target_lc = target.to_lowercase();
            let value = emit_expr(expr, states, msg);
            match route {
                crate::frontend::ast::Route::RoundRobin => format!(
                    "{indent}match self.{target_lc}_out.as_mut() {{\n\
                     {indent}    Some(out) => out.round_robin().send({value}).await?,\n\
                     {indent}    None => return Err(sigil_rt::SigilError::Transform(\"outbox to {target} not connected\".into())),\n\
                     {indent}}}\n"
                ),
                crate::frontend::ast::Route::ByKey(key) => {
                    let key_src = emit_expr(key, states, msg);
                    format!(
                        "{indent}match self.{target_lc}_out.as_ref() {{\n\
                         {indent}    Some(out) => out.by_key(&{key_src}).send({value}).await?,\n\
                         {indent}    None => return Err(sigil_rt::SigilError::Transform(\"outbox to {target} not connected\".into())),\n\
                         {indent}}}\n"
                    )
                }
                crate::frontend::ast::Route::Broadcast => format!(
                    "{indent}match self.{target_lc}_out.as_ref() {{\n\
                     {indent}    Some(out) => {{\n\
                     {indent}        let __b = {value};\n\
                     {indent}        for h in out.shards() {{\n\
                     {indent}            h.send(__b.clone()).await?;\n\
                     {indent}        }}\n\
                     {indent}    }}\n\
                     {indent}    None => return Err(sigil_rt::SigilError::Transform(\"outbox to {target} not connected\".into())),\n\
                     {indent}}}\n"
                ),
            }
        }
        Stmt::Expr { expr, .. } => {
            format!("{indent}let _ = {};\n", emit_expr(expr, states, msg))
        }
    }
}

fn is_timed_pipeline(expr: &Expr) -> bool {
    match expr {
        Expr::Pipeline { steps, .. } => steps.iter().any(|s| {
            s.tags
                .iter()
                .any(|t| matches!(t, Tag::Timeout { .. }))
        }),
        _ => false,
    }
}

fn emit_expr(expr: &Expr, states: &[String], msg: &str) -> String {
    match expr {
        Expr::Ident { name, .. } => {
            if states.iter().any(|s| s == name) {
                format!("self.{name}")
            } else {
                name.clone()
            }
        }
        Expr::FieldAccess { base, field, .. } => {
            if states.iter().any(|s| s == base) {
                format!("self.{base}.{field}")
            } else {
                format!("{base}.{field}")
            }
        }
        Expr::Literal { value, .. } => emit_literal(value),
        Expr::Call { name, args, .. } => {
            let a = args
                .iter()
                .map(|e| emit_expr(e, states, msg))
                .collect::<Vec<_>>()
                .join(", ");
            format!("{name}({a}).await?")
        }
        Expr::Binary { op, lhs, rhs, .. } => {
            let op_s = match op {
                BinOp::Add => "+",
                BinOp::Sub => "-",
                BinOp::Mul => "*",
                BinOp::Div => "/",
                BinOp::Le => "<=",
                BinOp::Ge => ">=",
                BinOp::Lt => "<",
                BinOp::Gt => ">",
                BinOp::Eq => "==",
            };
            format!(
                "({} {} {})",
                emit_expr(lhs, states, msg),
                op_s,
                emit_expr(rhs, states, msg)
            )
        }
        Expr::Pipeline { base, steps, .. } => emit_pipeline(base, steps, states, msg, false, ""),
    }
}

fn emit_expr_multiline(expr: &Expr, states: &[String], msg: &str, indent: &str) -> String {
    match expr {
        Expr::Pipeline { base, steps, .. } => {
            emit_pipeline(base, steps, states, msg, true, indent)
        }
        other => emit_expr(other, states, msg),
    }
}

fn emit_pipeline(
    base: &Expr,
    steps: &[crate::frontend::ast::PipeStep],
    states: &[String],
    msg: &str,
    multiline: bool,
    indent: &str,
) -> String {
    let mut current = emit_expr(base, states, msg);
    for step in steps {
        let transform = match &step.expr {
            Expr::Ident { name, .. } => name.clone(),
            Expr::Call { name, .. } => name.clone(),
            other => emit_expr(other, states, msg),
        };

        let timeout_ms = step.tags.iter().find_map(|t| match t {
            Tag::Timeout { expr, .. } => match expr {
                Expr::Literal {
                    value: Literal::DurationMs(ms),
                    ..
                } => Some(*ms),
                _ => None,
            },
            _ => None,
        });
        let recover = step.tags.iter().find_map(|t| match t {
            Tag::Recover { with, .. } => match with {
                Expr::Ident { name, .. } => Some(name.clone()),
                Expr::Call { name, .. } => Some(name.clone()),
                _ => None,
            },
            _ => None,
        });

        if let Some(ms) = timeout_ms {
            let fallback = recover.unwrap_or_else(|| transform.clone());
            if multiline {
                let inner = format!(
                    "{{\n\
{indent}    let __in = {current};\n\
{indent}    match timeout(Duration::from_millis({ms}), {transform}(__in.clone())).await {{\n\
{indent}        Ok(Ok(v)) => v,\n\
{indent}        Ok(Err(_)) | Err(_) => {{\n\
{indent}            sigil_rt::chaos::note_recovery(\"{transform}\");\n\
{indent}            {fallback}(__in).await?\n\
{indent}        }}\n\
{indent}    }}\n\
{indent}}}"
                );
                current = inner;
            } else {
                current = format!(
                    "({{ let __in = {current}; match timeout(Duration::from_millis({ms}), {transform}(__in.clone())).await {{ Ok(Ok(v)) => v, Ok(Err(_)) | Err(_) => {{ sigil_rt::chaos::note_recovery(\"{transform}\"); {fallback}(__in).await? }} }} }})"
                );
            }
        } else if let Some(fallback) = recover {
            // Untimed external stage with a declared recovery path.
            current = format!(
                "({{ let __in = {current}; match {transform}(__in.clone()).await {{ Ok(v) => v, Err(_) => {{ sigil_rt::chaos::note_recovery(\"{transform}\"); {fallback}(__in).await? }} }} }})"
            );
        } else {
            current = format!("{transform}({current}).await?");
        }
    }
    current
}

fn emit_literal(lit: &Literal) -> String {
    match lit {
        Literal::Int(i) => i.to_string(),
        Literal::Float(f) => {
            let s = format!("{f}");
            if s.contains('.') {
                s
            } else {
                format!("{s}.0")
            }
        }
        Literal::String(s) => format!("\"{s}\".to_string()"),
        Literal::Bool(b) => b.to_string(),
        Literal::DurationMs(ms) => format!("Duration::from_millis({ms})"),
    }
}

fn rust_type(ty: &Type) -> String {
    match ty {
        Type::Int => "i64".into(),
        Type::Float => "f64".into(),
        Type::String => "String".into(),
        Type::Bool => "bool".into(),
        Type::UUID => "String".into(),
        Type::Bytes => "Vec<u8>".into(),
        Type::Duration => "Duration".into(),
        Type::Named(n) => n.clone(),
    }
}

fn emit_external_stub(name: &str, in_ty: &str, out_ty: &str) -> String {
    // External residual stages route through sigil_rt::chaos so injected
    // latency and faults exercise the verified @timeout/@recover paths.
    if in_ty == out_ty {
        format!(
            "async fn {name}(input: {in_ty}) -> Result<{out_ty}> {{\n    sigil_rt::chaos::external_stage(\"{name}\").await?;\n    Ok(input)\n}}\n"
        )
    } else {
        // Stage changes schema: build a default output (residual: real logic belongs here).
        format!(
            "async fn {name}(_input: {in_ty}) -> Result<{out_ty}> {{\n    sigil_rt::chaos::external_stage(\"{name}\").await?;\n    Ok({out_ty}::default())\n}}\n"
        )
    }
}

fn collect_externals(program: &Program) -> BTreeSet<String> {
    let mut set = BTreeSet::new();
    for process in &program.processes {
        let mut locals: BTreeSet<String> =
            process.states.iter().map(|s| s.name.clone()).collect();
        for handler in &process.handlers {
            locals.insert(handler.msg_name.clone());
            for stmt in &handler.body {
                match stmt {
                    Stmt::Let { name, expr, .. } => {
                        walk_externals(expr, &mut set);
                        locals.insert(name.clone());
                    }
                    Stmt::Assign { expr, .. }
                    | Stmt::Send { expr, .. }
                    | Stmt::Expr { expr, .. } => {
                        walk_externals(expr, &mut set);
                    }
                }
            }
        }
    }
    set
}

fn walk_externals(expr: &Expr, set: &mut BTreeSet<String>) {
    match expr {
        Expr::Call { name, args, .. } => {
            set.insert(name.clone());
            for a in args {
                walk_externals(a, set);
            }
        }
        Expr::Pipeline { base, steps, .. } => {
            walk_externals(base, set);
            for step in steps {
                match &step.expr {
                    Expr::Ident { name, .. } | Expr::Call { name, .. } => {
                        set.insert(name.clone());
                    }
                    other => walk_externals(other, set),
                }
                for tag in &step.tags {
                    if let Tag::Recover { with, .. } = tag {
                        if let Expr::Ident { name, .. } = with {
                            set.insert(name.clone());
                        }
                    }
                }
            }
        }
        Expr::Binary { lhs, rhs, .. } => {
            walk_externals(lhs, set);
            walk_externals(rhs, set);
        }
        Expr::Ident { .. } | Expr::FieldAccess { .. } | Expr::Literal { .. } => {}
    }
}

fn emit_smoke_test(program: &Program) -> String {
    if program.processes.is_empty() {
        return String::new();
    }
    let process = &program.processes[0];
    let mut s = String::new();
    s.push_str("#[cfg(test)]\nmod tests {\n    use super::*;\n\n");
    s.push_str("    #[tokio::test]\n");
    s.push_str("    async fn smoke() {\n");
    s.push_str(&format!("        let mut p = {}::new();\n", process.name));
    if let Some(handler) = process.handlers.first() {
        let ty = rust_type(&handler.msg_ty);
        s.push_str(&format!("        let msg = {ty}::default();\n"));
        s.push_str(&format!(
            "        p.on_{}(msg).await.expect(\"handler should succeed\");\n",
            handler.msg_name
        ));
        // Observe local state was reachable (does not assert business values).
        for st in &process.states {
            s.push_str(&format!("        let _ = &p.{};\n", st.name));
        }
    }
    s.push_str("    }\n}\n");
    s
}


/// Multi-stage demo: spawn each process as a shard fleet in reverse
/// topological order, wire outboxes, feed the entry stage from concurrent
/// producers, then shut down stage by stage and print per-stage accounting.
fn emit_topology_main(program: &Program, topo: &crate::analysis::topology::Topology) -> String {
    use std::fmt::Write as _;
    let entry_name = topo
        .order
        .first()
        .cloned()
        .unwrap_or_else(|| program.processes[0].name.clone());
    let entry = program
        .processes
        .iter()
        .find(|p| p.name == entry_name)
        .expect("entry process exists");
    let entry_handler = entry.handlers.first().expect("entry handler");
    let msg_ty = rust_type(&entry_handler.msg_ty);

    // Message field synthesis (same policy as the fleet demo).
    let mut field_init = String::new();
    if let Type::Named(name) = &entry_handler.msg_ty {
        if let Some(schema) = program.schemas.iter().find(|sc| sc.name == *name) {
            for (fname, fty) in &schema.fields {
                match fty {
                    Type::Int => { let _ = writeln!(field_init, "                msg.{fname} = 1;"); }
                    Type::Float => { let _ = writeln!(field_init, "                msg.{fname} = 1.0;"); }
                    Type::String => { let _ = writeln!(field_init, "                msg.{fname} = format!(\"m-{{prod}}-{{i}}\");"); }
                    Type::Bool => { let _ = writeln!(field_init, "                msg.{fname} = true;"); }
                    _ => {}
                }
            }
        }
    }

    let edge_list = topo
        .edges
        .iter()
        .map(|e| format!("{} -> {} [{}]", e.from, e.to, e.msg_type))
        .collect::<Vec<_>>()
        .join(", ");

    let mut out = String::new();
    let _ = write!(out, "// AUTO-GENERATED by sigilc — multi-stage topology demo\n");
    let _ = write!(out, "//\n// Topology: {edge_list}\n");
    let _ = write!(out, "// Each stage is a fleet of shared-nothing actors; the compiler wired the\n");
    let _ = write!(out, "// outboxes, verified message types along every edge, proved the graph\n");
    let _ = write!(out, "// acyclic, and staged the shutdown so no message is ever lost in a closed\n// channel. No locks anywhere.\n\n");
    out.push_str("use sigil_gen::*;\nuse std::time::Instant;\n\n");
    out.push_str("#[tokio::main(flavor = \"multi_thread\")]\nasync fn main() {\n");
    out.push_str("    fn env_usize(key: &str, default: usize) -> usize {\n        std::env::var(key).ok().and_then(|v| v.parse().ok()).unwrap_or(default)\n    }\n");
    out.push_str("    let shards: usize = env_usize(\"SIGIL_DEMO_SHARDS\", 8);\n");
    out.push_str("    let producers: usize = env_usize(\"SIGIL_DEMO_PRODUCERS\", 64);\n");
    out.push_str("    let msgs_per_producer: usize = env_usize(\"SIGIL_DEMO_MSGS\", 250);\n");
    out.push_str("    let total = producers * msgs_per_producer;\n\n");

    // Spawn sinks first so upstream stages can be wired to live handles.
    for pname in topo.order.iter().rev() {
        let lc = pname.to_lowercase();
        let _ = write!(out, "    // stage: {pname}\n");
        let _ = write!(out, "    let mut {lc}_handles = Vec::new();\n    let mut {lc}_joins = Vec::new();\n");
        let _ = write!(out, "    for _i in 0..shards {{\n");
        let targets = topo.targets_of(pname);
        if targets.is_empty() {
            let _ = write!(out, "        let inst = {pname}::new();\n");
        } else {
            let _ = write!(out, "        let mut inst = {pname}::new();\n");
            for e in &targets {
                let tlc = e.to.to_lowercase();
                let _ = write!(
                    out,
                    "        inst.connect_{tlc}({tlc}_handles.clone());\n"
                );
            }
        }
        let _ = write!(out, "        let (h, j) = inst.spawn(1024);\n        {lc}_handles.push(h);\n        {lc}_joins.push(j);\n    }}\n\n");
    }

    let entry_lc = entry_name.to_lowercase();
    let _ = write!(out, "    let t0 = Instant::now();\n");
    let _ = write!(out, "    let mut producer_tasks = Vec::new();\n");
    let _ = write!(out, "    for prod in 0..producers {{\n        let hs = {entry_lc}_handles.clone();\n        producer_tasks.push(tokio::spawn(async move {{\n            for i in 0..msgs_per_producer {{\n                let mut msg = {msg_ty}::default();\n{field_init}                let shard = (prod + i) % hs.len();\n                if hs[shard].send(msg).await.is_err() {{\n                    break;\n                }}\n            }}\n        }}));\n    }}\n    for t in producer_tasks {{\n        let _ = t.await;\n    }}\n\n");

    let _ = write!(out, "    println!(\"=== topology demo: {edge_list} ===\");\n");
    out.push_str("    println!(\"{} shards/stage, {} producers, {} messages, {} worker threads\", shards, producers, total, std::thread::available_parallelism().map(|n| n.get()).unwrap_or(1));\n");

    // Shut down and report stage by stage in topological order.
    for pname in &topo.order {
        let lc = pname.to_lowercase();
        let process = program.processes.iter().find(|p| &p.name == pname).unwrap();
        let mut shard_fmt = String::new();
        let mut shard_args = String::new();
        let mut agg_decl = String::new();
        let mut agg_add = String::new();
        let mut agg_print = String::new();
        for st in &process.states {
            match st.ty {
                Type::Int => {
                    let _ = write!(agg_decl, "    let mut {lc}_agg_{}: i64 = 0;\n", st.name);
                    let _ = write!(agg_add, "        {lc}_agg_{} += st.{};\n", st.name, st.name);
                    let _ = write!(agg_print, "    println!(\"[{pname}] aggregate {} = {{}}\", {lc}_agg_{});\n", st.name, st.name);
                    let _ = write!(shard_fmt, " {}={{}}", st.name);
                    let _ = write!(shard_args, ", st.{}", st.name);
                }
                Type::Float => {
                    let _ = write!(agg_decl, "    let mut {lc}_agg_{}: f64 = 0.0;\n", st.name);
                    let _ = write!(agg_add, "        {lc}_agg_{} += st.{};\n", st.name, st.name);
                    let _ = write!(agg_print, "    println!(\"[{pname}] aggregate {} = {{:.1}}\", {lc}_agg_{});\n", st.name, st.name);
                    let _ = write!(shard_fmt, " {}={{:.1}}", st.name);
                    let _ = write!(shard_args, ", st.{}", st.name);
                }
                _ => {}
            }
        }
        let _ = write!(out, "\n    // ---- stage shutdown: {pname} ----\n");
        let _ = write!(out, "    drop({lc}_handles);\n");
        let _ = write!(out, "{agg_decl}");
        let _ = write!(out, "    let mut {lc}_handled: u64 = 0;\n    let mut {lc}_dropped: u64 = 0;\n");
        let _ = write!(out, "    for (idx, j) in {lc}_joins.into_iter().enumerate() {{\n        let (st, stats) = j.await.expect(\"actor task panicked\");\n        println!(\"[{pname}] shard {{idx}}:{shard_fmt} handled={{}} dropped={{}}\"{shard_args}, stats.handled, stats.dropped);\n{agg_add}        {lc}_handled += stats.handled;\n        {lc}_dropped += stats.dropped;\n    }}\n");
        let _ = write!(out, "{agg_print}");
        let _ = write!(out, "    println!(\"[{pname}] handled + dropped = {{}} + {{}} = {{}}\", {lc}_handled, {lc}_dropped, {lc}_handled + {lc}_dropped);\n");
    }

    let _ = write!(out, "\n    assert_eq!({entry_lc}_handled + {entry_lc}_dropped, total as u64, \"entry-stage message conservation violated\");\n");
    let _ = write!(out, "    println!(\"expected messages   = {{total}}\");\n");
    let _ = write!(out, "    println!(\"elapsed = {{:?}} (locks used: 0)\", t0.elapsed());\n");
    let _ = write!(out, "    println!(\"{{}}\", sigil_rt::chaos::report());\n}}\n");
    out
}

